'use strict';


// ----------
// 第1步：后端代理地址
// ----------

/**
 * 后端 AIGC 代理服务地址
 *
 * 【用大白话讲】
 *   这是前端要联系的后端服务器的地址。
 *   前端所有请求都发到这里，后端再帮忙转发到火山引擎 RTC。
 *
 *   格式：协议 + 域名 + 端口
 *   例如：http://localhost:3001
 *
 * 【生活中的比方】
 *   就像外卖平台的客服电话：
 *   有事打这个号码，前台帮你转接到各个部门。
 *
 * 【为什么需要代理？】
 *   火山引擎 RTC 的 API 有鉴权要求（签名），前端直接调用会暴露密钥。
 *   所以用后端服务器做代理，前端只管发请求，后端负责签名和转发。
 *
 * 【典型场景】
 *   发起请求时：
 *   fetch(`${AIGC_PROXY_HOST}?Action=getScenes`)
 *   → 实际请求：http://localhost:3001?Action=getScenes
 */
export const AIGC_PROXY_HOST = 'http://localhost:3001';


// ----------
// 第2步：设备类型枚举
// ----------

/**
 * 【枚举含义】设备类型的编号
 *
 * 【用大白话讲】
 *   枚举就是给常见值起个好记的名字。
 *   比如"0" = 麦克风，"1" = 摄像头。
 *   写代码时用名字，不用数字，代码更可读。
 *
 * 【为什么用枚举？】
 *   不用枚举的话：
 *     getDevices({ '0': true })  // "0"是什么意思？鬼知道
 *   用枚举的话：
 *     getDevices({ [DeviceType.Microphone]: true })  // 清晰！要麦克风
 *
 * 【字段含义】
 *   Microphone : 0 = 麦克风（录音用的）
 *   Camera    : 1 = 摄像头（录像用的）
 *
 * 【生活中的比方】
 *   就像部门编号：
 *   - 0 = 技术部
 *   - 1 = 市场部
 *   写"技术部"比写"0"好理解多了。
 */
export enum DeviceType {
    /** 麦克风（编号0） */
    Microphone = 0,
    /** 摄像头（编号1） */
    Camera = 1,
}
