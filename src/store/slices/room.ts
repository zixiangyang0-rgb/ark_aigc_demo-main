// =============================================================
// 第二部分：类型定义（档案的"字段说明"）
// =============================================================
//
// 在动手填档案之前，先定义好档案的"填写规范"（TypeScript 接口）。
// 就像档案室给每种档案印好了标准表格，告诉大家每个字段应该填什么、填什么格式。
// 这些接口（interface）描述了每种数据的结构，确保代码里不会出现"填错了字段"的情况。
// 如果填错了，TypeScript 编译器会报错，就像填表时格式不对会被退回来一样。
//
// 接口（interface）就像一张张"标准表格"：
//   IUser = 用户信息表
//   Msg = 字幕记录表
//   SceneConfig = 场景配置表
//   RTCConfig = 入场凭证表
//   RoomState = 房间总清单
//
// 每张表上都有固定的栏目，填写时必须按栏目来。
// 这样所有程序员都按同一套格式走，代码就不会乱。


/**
 * 【IUser 接口】房间里的"用户信息卡片"
 *
 * 【生活比方】想象一张来宾信息卡片，放在会议室的座位牌上：
 *   - 这人叫什么名字（username）
 *   - 身份证号（userId）
 *   - 现在有没有在说话（publishAudio）
 *   - 有没有开着摄像头（publishVideo）
 *   - 有没有共享屏幕（publishScreen）
 *   - 说话声音大不大、网络好不好（audioStats、audioPropertiesInfo）
 *
 * 【详细解释】
 *   username：用户在界面上显示的名字，比如"张三"或者"懂小智"。
 *             就像档案卡上的"姓名"栏，让人一眼认出是谁。
 *             可以重名，所以不能单独用这个名字来区分用户。
 *
 *   userId：用户在 RTC 房间里的唯一标识符，是后端分配的数字/字符串。
 *            就像档案的"编号"，用于精确找到某个人。
 *            比名字更准确，因为名字可能重名，但 ID 一定是唯一的。
 *
 *   publishAudio：是否正在发布音频流（麦克风是否开启）。
 *                 想象一个"正在通话中"的指示灯——亮着表示对方能听到这个人的声音。
 *                 就像会议室里某人的麦克风指示灯，亮着表示正在说话或者麦克风开着。
 *
 *   publishVideo：是否正在发布视频流（摄像头是否开启）。
 *                想象视频通话中的"画面"——开着就能看到这个人，关着就只有声音。
 *                就像会议室里某人面前的摄像头，亮着表示有画面。
 *
 *   publishScreen：是否正在共享自己的屏幕。
 *                  想象"屏幕共享"按钮——开着的话，大家都能看到这个人的电脑桌面。
 *                  就像投影仪正在投射谁的屏幕，谁的桌面就出现在大家眼前。
 *
 *   audioStats：远端音频的统计信息，包括：
 *               - 网络质量（信号好不好）
 *               - 丢包率（有多少话说了一半没传到）
 *               - 延迟（你说的话几秒后才被对方听到）
 *               就像通话质量的"质检报告"，让系统知道对方的语音流稳不稳定。
 *               这个字段只有远端用户才有（RemoteAudioStats）。
 *
 *   audioPropertiesInfo：音频属性信息，最主要的是音量大小（喷泉音量值）。
 *                        想象一个声浪仪，实时显示这个人说话有多大声。
 *                        数值越大说明声音越大，可以用来做"谁在说话"的指示器。
 */
export interface IUser {
    /** 用户显示名称（界面上看到的名字，比如"张三"、"懂小智"） */
    username?: string;
    /** 用户唯一ID（后端分配的身份编号，用于精确区分用户） */
    userId?: string;
    /** 是否发布音频流（麦克风是否开启，亮着=对方能听到声音） */
    publishAudio?: boolean;
    /** 是否发布视频流（摄像头是否开启，开着=能看到画面） */
    publishVideo?: boolean;
    /** 是否共享屏幕（屏幕共享是否开启，开着=大家能看到桌面） */
    publishScreen?: boolean;
    /** 远端音频统计（网络质量、丢包率、延迟等质检数据） */
    audioStats?: RemoteAudioStats;
    /** 音频属性信息（音量大小等，用于显示谁在说话） */
    audioPropertiesInfo?: AudioPropertiesInfo;
}


/**
 * 【LocalUser 类型】"我"的用户信息卡片（比普通用户多了一些字段）
 *
 * 【生活比方】LocalUser 就是"我"的档案卡片。
 *   和普通用户卡片一样有名字、摄像头状态等。
 *   但多了一样东西——登录凭证（loginToken），就像"员工工卡"。
 *   而且 audioStats 类型不一样：
 *     本地用户用 LocalAudioStats（记录"我"这边的情况）。
 *     远端用户用 RemoteAudioStats（记录"别人"那边的情况）。
 *   就像"我"的设备状态报告和"对方"的设备状态报告是两回事。
 *
 * 【详细解释】
 *   loginToken：登录凭证，用户加入房间的身份证明。
 *              想象公司的门禁卡——证明你有权进入这个房间。
 *              没有这张卡，闸机不会开，你就进不去。
 *
 *   audioStats：本地音频统计（LocalAudioStats）。
 *              记录的是"我"这边的通话质量，比如：
 *              - 我的麦克风音量怎么样
 *              - 我的网络上传速度怎么样
 *              就像"我"的设备体检报告。
 */
export type LocalUser = Omit<IUser, 'audioStats'> & {
    /** 登录凭证（进入房间的身份证明，没有这个进不了门） */
    loginToken?: string;
    /** 本地音频统计（"我"这边的通话质量数据） */
    audioStats?: LocalAudioStats;
};


/**
 * 【Msg 接口】"字幕记录卡片"——记录每一句说过的话
 *
 * 【生活比方】每说一句话，就在档案里新增一张"字幕卡片"。
 *   卡片上写着：这句话是谁说的、说了什么、什么时候说的。
 *   还标记这句话有没有说完、有没有被用户打断。
 *
 *   这就像电话客服的"通话记录单"：
 *   - 记录了客服说的每一句话。
 *   - 每句话标上时间。
 *   - 如果客服还在说，记录实时更新。
 *   - 如果客服说完了，标记"已完整"。
 *   - 如果你打断了客服，标记"被中断"。
 *
 * 【详细解释】
 *   value：消息的文本内容，就是 AI 或用户说的那段话本身。
 *          就像通话记录单上的"通话内容"栏，记录具体说了什么。
 *          例如："好的，您的订单编号是 12345，退款将在 1-3 个工作日内到账。"
 *
 *   time：这条消息产生的时间戳，用于在界面上显示"这句话是什么时候说的"。
 *         就像通话记录单上的"时间"栏。
 *         格式是 Date.toString()，比如"Sun Jun 07 2026 19:32:00 GMT+0800"。
 *
 *   user：说话人的 ID，用来标识这句话是谁说的。
 *         "AiAgent" = AI 说的话。
 *         "Huoshan01" = 用户说的话。
 *         就像通话记录单上的"发言人"栏，标明是谁说的。
 *
 *   paragraph：是否是新段落（是否新开一句话）。
 *              true = 这是新的一句话，和上一句是分开的。
 *              false = 这是上一句的继续，还没说完。
 *              想象速记员记笔记：
 *                客服说"好的，"（paragraph=true，新开一行）
 *                然后继续说"我来帮你查一下"（paragraph=false，继续上一行）
 *              就像写文章时的"分段"——新段落意味着前面那段已经结束了。
 *
 *   definite：这句话是否完整（AI 是否还在说）。
 *             true = AI 已经说完了，这是一句完整的话。
 *             false = AI 还在说，这句话还没说完，字幕会继续追加内容。
 *             想象：
 *               客服回答"好的，我来帮你查一下订单"（definite=true，说完了）
 *               客服回答到一半"好的，我来帮你..."（definite=false，还在说）
 *             就像写文章时有没有画句号——画了句号就是完整的一句话。
 *
 *   isInterrupted：这句话是否被用户打断。
 *                  true = 用户打断了 AI，这句话说到一半被打断了。
 *                  false = AI 正常说完的。
 *                  想象：
 *                    客服正在说"您的订单已经发..."
 *                    你插嘴说"等下，不用查了" → 客服的话被标记为"被打断"。
 *                  就像录音被打断后，录音笔会标记"此段被中断"。
 */
export interface Msg {
    /** 消息文本（说的具体内容，比如"好的，您的订单是12345"） */
    value: string;
    /** 消息时间（什么时候说的，Date.toString() 格式的时间戳） */
    time: string;
    /** 说话人ID（谁说的，AI="AiAgent"，用户=具体用户名） */
    user: string;
    /** 是否是新段落（true=新开一句，false=继续上一句） */
    paragraph?: boolean;
    /** 是否完整（true=说完了，false=还在说，会继续追加） */
    definite?: boolean;
    /** 是否被打断（true=被用户打断了，false=正常说完的） */
    isInterrupted?: boolean;
}


/**
 * 【SceneConfig 接口】"场景配置卡片"——定义每个 AI 场景的配置
 *
 * 【生活比方】每个场景就像一个"工作岗位说明书"。
 *   比如"懂小智"这个岗位有：
 *   - 穿什么衣服（icon 头像）
 *   - 叫什么名字（name 显示名称）
 *   - 会回答什么问题（questions 预设问题）
 *   - 有没有摄像头（isVision 视觉模式）
 *   - 能不能被打断（isInterruptMode）
 *   - 是不是数字人（isAvatarScene）
 *
 *   切换场景就像换了一个客服：
 *   换一个客服，就要换一份新的工作说明书。
 *   有些客服能看视频（视觉模式），有些只能听声音。
 *   有些客服允许你随时打断，有些你得等他说完了才能开口。
 *
 * 【详细解释】
 *   id：场景的编号，区分不同的场景。
 *       比如 "Custom"（自定义）、"Agent"（智能体）、"Doctor"（医生）等。
 *       就像每个工作岗位的"岗位编号"。
 *
 *   icon：AI 头像的图片地址，用户在界面上看到 AI 的头像就是这个。
 *         就像客服的"工牌照片"，让人一眼认出是谁。
 *
 *   name：AI 的显示名称，比如"懂小智"、"智能助手"、"在线医生"。
 *         就像客服的"工牌姓名"，界面上显示的名字。
 *
 *   questions：预设问题列表，展示在界面上供用户点击快速提问。
 *              就像客服常被问到的"FAQ 问题列表"：
 *              "怎么退款？"、"订单在哪？"、"怎么修改地址？"
 *              用户不用打字，直接点一下就问出问题了。
 *
 *   botName：AI 在 RTC 房间里的用户名，用于区分 RTC 房间里的各个用户。
 *            这个名字是 RTC 系统内部用的，不同于界面上显示的 name。
 *            想象客服有"内部工号"（系统用）和"对外称呼"（客人看到的是不一样的）。
 *            例如：界面上显示"懂小智"（name），但系统里叫"AiAgent_Custom"（botName）。
 *
 *   isVision：是否开启视觉模式，支持摄像头输入和屏幕共享。
 *             true = AI 能"看见"用户的摄像头画面或屏幕内容。
 *             false = AI 只能听声音，看不见画面。
 *             想象客服配备了"视频通话"能力，可以看到你的画面。
 *             例如：AI 可以看到你的商品实物图，然后帮你分析。
 *
 *   isScreenMode：是否开启屏幕共享模式。
 *                  true = 用户可以共享自己的屏幕给 AI 看。
 *                  false = 用户不能共享屏幕。
 *                  想象客服有"屏幕共享"功能，可以看到你的电脑桌面。
 *                  例如：用户共享屏幕给 AI 看，AI 可以帮用户操作电脑。
 *
 *   isInterruptMode：是否允许用户打断 AI 说话。
 *                     true = 用户可以在 AI 说话时插嘴打断。
 *                     false = AI 说话时用户只能听着，不能打断。
 *                     想象：
 *                       开启了"可打断模式"的客服，你随时可以插话。
 *                       关闭了这个模式的客服，你得等他说完了才能开口。
 *                     就像有些客服热线允许你"插话"，有些必须等客服说完。
 *
 *   isAvatarScene：是否是数字人场景。
 *                  true = 这个场景用的是数字人（AI 虚拟形象）。
 *                  false = 普通语音对话场景（没有虚拟形象）。
 *                  想象：这个岗位是"数字人客服"还是"纯语音客服"。
 *                  数字人有形象、有动作，普通语音只有声音。
 *
 *   avatarBgUrl：数字人背景图的 URL，数字人场景的背景图片。
 *                 想象数字人客服的"舞台背景布"——站在什么背景前。
 *                 就像新闻主持人在绿幕前，背景是后期合成上去的。
 *                 不同的背景给人不一样的氛围感。
 */
export interface SceneConfig {
    /** 场景 ID（场景的编号，用于区分不同场景，如"Custom"、"Agent"） */
    id: string;
    /** AI 头像 URL（客服的照片，界面上显示的 AI 头像就是这个） */
    icon?: string;
    /** AI 显示名称（客服叫什么，界面上看到的名字） */
    name?: string;
    /** 预设问题列表（FAQ 问题，用户点击快速提问） */
    questions?: string[];
    /** AI 的 RTC 用户名（系统内部用的身份标识，不同于界面的 name） */
    botName: string;
    /** 是否支持视觉模式（true=AI能看见摄像头/屏幕，false=只能听声音） */
    isVision: boolean;
    /** 是否支持屏幕共享（true=用户可以共享屏幕给AI看） */
    isScreenMode: boolean;
    /** 是否允许打断（true=用户可以随时插嘴，false=必须等AI说完） */
    isInterruptMode: boolean;
    /** 是否是数字人场景（true=用虚拟形象，false=纯语音） */
    isAvatarScene: boolean;
    /** 数字人背景图 URL（数字人站在什么背景前） */
    avatarBgUrl: string;
}


/**
 * 【RTCConfig 接口】"入场凭证卡片"——加入实时通话房间的钥匙
 *
 * 【生活比方】RTCConfig 就像是进入一间会议室（RTC 房间）的入场券。
 *   想象你收到一张会议邀请，上面写着：
 *   - 会议室在哪（RoomId）
 *   - 证明你是谁（UserId）
 *   - 证明你有权限进去（Token）
 *
 *   这张券是后端签发的，就像公司前台给访客发的临时门禁卡。
 *   没有这张卡，你连门都进不去。
 *
 * 【详细解释】
 *   AppId：RTC 应用的唯一标识符，在火山引擎控制台创建应用时生成。
 *          想象"会议室所属的分公司编号"——不同 AppId 属于不同的应用/项目。
 *          就像哔哩哔哩的会议室和抖音的会议室是不同的地方。
 *
 *   RoomId：房间号，所有参与者在同一个房间里才能互相通话。
 *           想象"会议室门牌号"——1001 号会议室和 1002 号会议室是完全不同的房间。
 *           就像加入微信群，你需要知道群号才能进去。
 *
 *   UserId：当前用户在房间里的唯一身份标识。
 *           后端根据 AppId + UserId 来签发 Token。
 *           想象"员工编号"——在同一栋楼（AppId）里，每个员工的编号是唯一的。
 *           不同 AppId 下面可以有相同的 UserId，但它们代表不同的人。
 *
 *   Token：加入房间的凭证/令牌，由后端用 AppId 等信息签名生成。
 *          有效期有限，过期了需要重新申请。
 *          想象"门禁卡"——有这张卡才能刷卡进门，卡过期了要去前台续期。
 *          签发 Token 的目的：确保只有合法用户才能进入房间。
 */
export interface RTCConfig {
    /** RTC 应用 ID（所属项目的编号，区分不同应用） */
    AppId: string;
    /** 房间号（进入哪个会议室，和对方在同一个房间才能通话） */
    RoomId: string;
    /** 用户 ID（证明你是谁，在同一个AppId下唯一） */
    UserId: string;
    /** 入场 Token（门禁卡，有了这张卡才能进门，过期要重新申请） */
    Token: string;
}


/**
 * 【RoomState 接口】"房间档案柜"——记录整个通话房间的完整状态
 *
 * 【生活比方】这相当于档案柜里的"房间总览清单"，上面列出了一切：
 *   - 房间号是什么
 *   - 现在是几点（通话多久了）
 *   - 有几个人在房间里
 *   - AI 是闲着还是在说话
 *   - 对话说了哪些内容
 *   - 字幕显示不显示
 *
 *   这份清单是"当前状态"的快照，每次通话结束或者重启，数据就重置了。
 *   就像拍完一场戏后，场记板上的信息就归档保存，准备下一场。
 *
 * 【详细解释】
 *   time：通话已经持续的时长，单位是秒。
 *         想象通话计时器——从加入房间开始计数，挂断时停止。
 *         例如：time=125 表示通话已经进行了 125 秒（2 分 5 秒）。
 *         初始值是 -1，表示还没开始计时。
 *
 *   roomId：当前所在房间的编号。
 *           想象"会议室门牌号"。
 *           例如："Room_2024_06_07_001"。
 *
 *   localUser："我"的档案卡片，记录我自己的状态。
 *              想象档案柜里放着一张"我自己"的卡片，上面记录了我的状态。
 *              包括：用户名、麦克风开没开、摄像头开没开、屏幕共享开没开。
 *
 *   remoteUsers：房间里除了我以外的所有人的卡片列表。
 *                AI 也是一个"远端用户"——在 RTC 层面，AI 和其他用户是一样的。
 *                想象会议室里除了你以外的所有人的座位牌。
 *                每个人一张卡片，记录着他的状态。
 *
 *   autoPlayFailUser：因为浏览器自动播放策略导致音频播放失败的用户 ID 列表。
 *                     某些浏览器（Chrome、Safari）不允许自动播放声音，
 *                     用户必须先点击页面才能出声。
 *                     想象：会议室里有些人因为设备问题听不到声音（需要手动处理）。
 *                     界面通常会弹出一个提示："请先点击页面以启用音频"。
 *
 *   isJoined：是否已经成功加入了房间。
 *             true = 已经在房间里了，可以通话。
 *             false = 还没有加入（或已经离开了）。
 *             想象：是否已经进了会议室的门。
 *
 *   scene：当前选中的场景 ID，比如 "Custom" 或 "Agent"。
 *          想象：当前会议室里用的是哪个"服务方案"。
 *          不同的场景有不同的 AI 配置，就像不同的会议室有不同的用途。
 *
 *   sceneConfigMap：场景 ID → 场景配置 的对照表。
 *                    存储了所有可用场景的完整配置。
 *                    想象：档案柜里有多个"岗位说明书"，按编号（场景ID）索引。
 *                    例如：
 *                    {
 *                      "Custom": { id, name, botName, isVision, ... },
 *                      "Agent": { id, name, botName, isVision, ... }
 *                    }
 *
 *   rtcConfigMap：场景 ID → RTC 配置 的对照表。
 *                  存储了每个场景对应的入场凭证。
 *                  想象：每个岗位（场景）对应一套进入权限（门禁卡）。
 *                  例如：
 *                  {
 *                    "Custom": { AppId, RoomId, UserId, Token },
 *                    "Agent": { AppId, RoomId, UserId, Token }
 *                  }
 *
 *   isAIGCEnable：AI 通话功能是否已经启用。
 *                  true = AI 功能已开启，可以开始对话。
 *                  false = AI 功能未开启或已关闭。
 *                  想象：客服系统是否已经开机，准备好接听电话了。
 *
 *   isAITalking：AI 是否正在说话（输出语音/字幕）。
 *                true = AI 正在说话，字幕在实时追加。
 *                false = AI 没有在说话（可能闲着或在想）。
 *                想象：客服正在电话里说话——嘴在动。
 *
 *   isAIThinking：AI 是否正在思考（正在处理/生成回复）。
 *                  true = AI 正在内部处理，还没开始输出。
 *                  false = AI 没有在思考（已经想好了或没在生成回复）。
 *                  想象：客服正在脑子里想答案——脑子在转。
 *
 *   isUserTalking：用户是否正在说话（是否有声音输入）。
 *                   true = 检测到用户在说话/输入语音。
 *                   false = 用户没有在说话。
 *                   想象：电话里你正在说话，客服听到你的声音。
 *
 *   networkQuality：网络质量评级，数值越小越好。
 *                   0=UNKNOWN（未知，还没开始通话）
 *                   1=EXCELLENT（极好，信号满格）
 *                   2=GOOD（良好，偶尔轻微卡顿）
 *                   3=POOR（较差，经常卡顿）
 *                   4=BAD（差，通话断断续续）
 *                   5=VBAD（极差，几乎无法通话）
 *                   想象：通话线路的"信号强度"——1格到5格信号。
 *
 *   msgHistory：所有说过的"字幕卡片"的列表，按时间顺序排列。
 *               AI 说的和用户说的都会记录在这里。
 *               想象：客服的"通话记录本"，记录了所有对话内容。
 *               每当有人说一句话，就新增一条记录。
 *               如果一句话还没说完，会不断追加内容到同一条记录里。
 *
 *   currentConversation：当前正在进行的对话（实时文本流）。
 *                         存储每个人正在实时输出的内容。
 *                         想象：客服说话时，实时显示的"正在输入中..."。
 *                         例如：
 *                         {
 *                           "AiAgent": { msg: "好的，我来", definite: false },
 *                           "Huoshan01": { msg: "谢谢", definite: true }
 *                         }
 *                         definite=false 说明 AI 还在说，字幕会继续追加。
 *                         definite=true 说明这句话说完了。
 *
 *   isShowSubtitle：字幕功能是否开启。
 *                   true = 显示字幕。
 *                   false = 隐藏字幕。
 *                   想象：电话通话时，显示/隐藏实时字幕功能。
 *                   开启后，AI 和用户说的话都会实时显示成文字。
 *
 *   isFullScreen：数字人/主界面是否全屏显示。
 *                  true = 全屏（画面占满整个屏幕）。
 *                  false = 窗口模式（画面在页面中占一部分）。
 *                  想象：视频通话时，是否把画面放大到整个屏幕。
 *
 *   customSceneName：用户自定义的场景名称。
 *                     如果用户改了一个 AI 的名字（比如把"懂小智"改成"小王"）
 *                     就存在这里。
 *                     想象：会议室被重新命名为"张三专属会议室"。
 */
export interface RoomState {
    /** 通话时长（秒，初始值 -1 表示还没开始计时） */
    time: number;
    /** 房间号（会议室门牌号，用于区分不同房间） */
    roomId?: string;
    /** "我"的档案卡片（记录自己的状态：用户名、设备开关等） */
    localUser: LocalUser;
    /** 其他所有人的卡片列表（除了我以外的所有人） */
    remoteUsers: IUser[];
    /** 音频播放失败的用户 ID 列表（浏览器自动播放策略导致） */
    autoPlayFailUser: string[];
    /** 是否已加入房间（true=已经进门，false=还没进来） */
    isJoined: boolean;
    /** 当前场景 ID（当前用的是哪个场景配置，如"Custom"、"Agent"） */
    scene: string;
    /** 场景配置对照表（场景ID → 场景配置，存储所有场景的完整配置） */
    sceneConfigMap: Record<string, SceneConfig>;
    /** RTC 配置对照表（场景ID → 入场凭证，每个场景的入房钥匙） */
    rtcConfigMap: Record<string, RTCConfig>;
    /** AI 功能是否启用（true=开机，false=关机） */
    isAIGCEnable: boolean;
    /** AI 是否正在说话（true=嘴在动，false=没说话） */
    isAITalking: boolean;
    /** AI 是否正在思考（true=脑子在转，false=没在想） */
    isAIThinking: boolean;
    /** 用户是否正在说话（true=检测到用户声音，false=用户没说话） */
    isUserTalking: boolean;
    /** 网络质量（1=极好，5=极差，0=未知） */
    networkQuality: NetworkQuality;
    /** 字幕历史列表（所有说过的话，按时间顺序排列） */
    msgHistory: Msg[];
    /** 当前实时对话（正在说的内容，实时更新的文本） */
    currentConversation: {
        [user: string]: {
            /** 实时文本内容（当前正在说的这段话） */
            msg: string;
            /** 是否完整（true=说完了，false=还在说，会继续追加） */
            definite: boolean;
        };
    };
    /** 是否显示字幕（true=显示，false=隐藏） */
    isShowSubtitle: boolean;
    /** 是否全屏（true=全屏，false=窗口模式） */
    isFullScreen: boolean;
    /** 自定义场景名称（用户给 AI 起的自定义名字） */
    customSceneName: string;
}


// =============================================================
// 第三部分：初始状态（档案柜的"出厂状态"）
// =============================================================
//
// 定义档案柜刚买回来、还没放进任何档案时的"出厂状态"。
// 就像新开一间会议室：灯是关的、椅子是空的、投影仪是关闭的。
// 等有人加入房间了，档案才会一条一条填进去。
//
// 【生活比方】初始状态就像"空白档案柜"
//   档案柜买回来的时候：
//   - 所有的抽屉都是空的
//   - 没有用户卡片
//   - 没有字幕记录
//   - AI 功能是关闭的
//   - 时长是 -1（还没开始计时）
//
//   当用户点击"加入房间"后：
//   - 档案柜开始被填入内容
//   - 用户卡片被放进去
//   - AI 被邀请进房间
//   - 计时器开始走
//
//   当用户离开房间后：
//   - 档案柜被清空
//   - 恢复到这个初始状态
//
//   这样设计的好处：
//   - 每次开始新的通话，都是从干净的状态起步
//   - 不会把上一次通话的数据带到下一次
const initialState: RoomState = {
    // 通话时长初始为 -1，表示还没开始计时。
    // 正式计时后会变成 0, 1, 2, 3... 秒。
    // 挂断后会重置为 -1，表示"不再计时"。
    // 想象会议室的计时器，刚买来时显示"--:--"，有人进去才开始走。
    time: -1,

    // 当前场景初始为空字符串。
    // 等用户选择了场景（比如点击"自定义场景"）之后才会填入具体值。
    // 如果是空字符串，说明用户还没有选择任何场景。
    scene: '',

    // 场景配置映射初始为空对象 {}。
    // 程序启动后，会调用 getScenes 接口从后端获取所有场景的配置。
    // 获取到之后，这个空对象会被后端返回的数据填充。
    // 就像档案柜里的"场景配置"抽屉一开始是空的，
    // 初始化时会有人把资料按编号分类放进来。
    sceneConfigMap: {},

    // RTC 配置映射初始为空对象 {}，同样由 getScenes 获取并填入。
    // 每个场景对应一套入场凭证（AppId、RoomId、UserId、Token）。
    // 就像档案柜里的"入场凭证"抽屉一开始是空的。
    rtcConfigMap: {},

    // 远端用户列表初始为空数组 []。
    // 加入房间后，会把 AI 用户和其他用户加进这个列表。
    // 离开房间后，这个列表会被清空。
    remoteUsers: [],

    // "我"（本地用户）的初始状态：默认关闭所有媒体设备。
    // 刚进入会议室，默认不说话、不露脸、不共享屏幕——要主动开启。
    // 就像新员工入职，默认没有任何权限，需要一个个申请开通。
    localUser: {
        // 默认关闭麦克风（不发布音频）
        // 刚进会议室，默认不打算发言，要自己按按钮开启。
        publishAudio: false,
        // 默认关闭摄像头（不发布视频）
        // 刚进会议室，默认不打算露脸，要自己按按钮开启。
        publishVideo: false,
        // 默认关闭屏幕共享
        // 刚进会议室，默认不打算共享屏幕，要自己按按钮开启。
        publishScreen: false,
    },

    // 自动播放失败的用户列表初始为空数组 []。
    // 当有用户因为浏览器自动播放策略无法播放声音时，会把用户名加进来。
    // 如果为空，说明没有用户遇到这个问题。
    autoPlayFailUser: [],

    // 是否已加入房间初始为 false。
    // 还没进门，只有调用了 localJoinRoom 后才会变成 true。
    isJoined: false,

    // AI 功能初始为关闭。
    // 还没开始 AI 对话，只有用户点击"开始对话"后才会开启。
    isAIGCEnable: false,

    // AI 思考状态初始为 false。
    // AI 还没开始思考（还没开始处理对话），只有开始处理用户问题后才会变成 true。
    isAIThinking: false,

    // AI 说话状态初始为 false。
    // AI 还没开始说话，只有开始输出回复后才会变成 true。
    isAITalking: false,

    // 用户说话状态初始为 false。
    // 用户还没开始说话，只有检测到用户声音后才会变成 true。
    isUserTalking: false,

    // 网络质量初始为"未知"（UNKNOWN）。
    // 还没开始通话所以测不出来，开始通话后会有实际的数值。
    networkQuality: NetworkQuality.UNKNOWN,

    // 对话历史初始为空数组 []。
    // AI 开始说话后，这个列表会一条一条增加字幕卡片。
    // 就像通话记录本，一开始是空白的，说完话后才一页一页记上去。
    msgHistory: [],

    // 当前实时对话初始为空对象 {}。
    // 还没开始实时输入，开始说话后会有正在说的内容。
    currentConversation: {},

    // 字幕显示初始为 true（默认显示字幕）。
    // 用户可以选择关闭字幕（隐藏实时对话文字）。
    // 就像有些视频网站默认显示字幕，你可以手动关掉。
    isShowSubtitle: true,

    // 全屏状态初始为 false（默认窗口模式显示）。
    // 用户可以点击全屏按钮切换到全屏模式。
    isFullScreen: false,

    // 自定义场景名称初始为空字符串。
    // 没有自定义名称时，使用场景默认的名字（如"懂小智"）。
    customSceneName: '',
};


// =============================================================
// 第四部分：创建房间 Slice（建立档案管理规则）
// =============================================================
//
// 通过 createSlice 创建房间状态的"档案管理系统"。
// 里面定义了所有的"档案更新规则"（reducers）。
// 每条规则处理一种特定的更新申请，告诉管理员遇到这种申请应该怎么改档案。
//
// 【生活比方】这部分就是给档案管理员制定"工作手册"：
//   工作手册上写着：
//   - "收到加入房间申请，就把 isJoined 改成 true"
//   - "收到离开房间申请，就把所有状态清空"
//   - "收到字幕追加申请，就往 msgHistory 里加一条记录"
//   ...
//
//   管理员严格按照工作手册办事，不会自作主张。
//   这样做的好处是：所有状态变更都是可预测的，不会乱。
export const roomSlice = createSlice({
    // 这个 Slice 的名字叫 "room"。
    // 用于在 Redux store 中区分不同的 Slice（不同的档案抽屉）。
    // 就像给档案柜贴上"房间档案"的标签，财务部抽屉贴"财务"标签。
    name: 'room',

    // 指定初始状态——档案柜买回来时的默认状态。
    // 就是上一部分定义的 initialState。
    // 每次 store 创建时，都从这堆默认值开始。
    initialState,

    // 定义所有的"档案更新规则"（reducers）。
    // 每条规则对应一种用户申请，处理特定的状态更新。
    // 格式是：reducers: { 规则名: (state, action) => { 具体操作 } }
    //
    // 【生活比方】工作手册上的每一条操作规程：
    //   "规程一：处理加入房间申请"
    //   "规程二：处理离开房间申请"
    //   ...
    reducers: {


        // =========================================================
        // 规程一：加入房间（localJoinRoom）
        // =========================================================
        //
        // 【Action 含义】本地用户申请加入房间
        //
        // 【生活比方】你拿着会议邀请函，到达会议室门口，刷卡进门。
        //             刷卡成功，闸机打开，你就正式进入了会议室。
        //
        // 【申请内容】payload = {
        //   roomId: "12345",        // 要进哪个房间（会议室门牌号）
        //   user: {
        //     userId: "Huoshan01",  // 你的用户ID（员工编号）
        //     username: "张三",      // 你的显示名称（工牌姓名）
        //     loginToken: "xxx"     // 登录凭证（门禁卡）
        //   }
        // }
        //
        // 【档案更新内容】
        //   1. roomId → 填入房间号（现在知道自己在哪个房间了）
        //   2. localUser → 把身份信息合并到"我"的档案里（姓名、ID、Token等）
        //   3. isJoined → true（已加入，可以开始通话了）
        //
        // 【副作用】这是"进入会议室"的唯一入口。
        //          只有调用了这个方法，isJoined 才会变成 true。
        //          才算是正式加入了 RTC 房间，可以收发音频视频了。
        localJoinRoom: (state, { payload }) => {
            // 记录要进入的房间号
            // 就像在档案上写"当前所在会议室：1001室"
            state.roomId = payload.roomId;

            // 更新"我"的档案卡片
            // 想象把门禁卡上的信息写进档案里
            state.localUser = {
                // 先保留原有的默认值
                // 比如 publishAudio=false 这些初始设置不能丢
                ...state.localUser,
                // 再合并传入的新数据（用户ID、姓名、Token等）
                // 传入的数据会覆盖同名字段
                ...payload.user,
            };

            // 标记为"已加入"，可以开始通话了
            // 就像闸机显示绿灯，门开了
            state.isJoined = true;
        },


        // =========================================================
        // 规程二：离开房间（localLeaveRoom）
        // =========================================================
        //
        // 【Action 含义】本地用户申请离开房间
        //
        // 【生活比方】你离开会议室，关掉摄像头、麦克风，收拾东西走人。
        //             就像会议结束后离开，会议室恢复初始状态，等待下一场会议。
        //
        // 【档案更新内容】
        //   1. roomId → undefined（不再属于任何房间）
        //   2. time → -1（通话计时器归零重置）
        //   3. localUser → 恢复默认值（关麦克风、关摄像头、关屏幕共享）
        //   4. remoteUsers → []（清空，房间里没人了）
        //   5. isJoined → false（已离开，不再是房间成员）
        //
        // 【副作用】离开房间后，所有与该房间相关的状态都被清理。
        //          这是"退出会议室"的唯一出口。
        localLeaveRoom: (state) => {
            // 离开房间，不再有房间号
            // 就像档案上把"当前会议室"划掉
            state.roomId = undefined;

            // 重置通话时长为初始值 -1
            // 就像会议室的计时器归零
            state.time = -1;

            // 重置"我"的状态为默认值
            // 就像离开时关掉所有设备、收拾好个人物品
            state.localUser = {
                // 关闭麦克风
                publishAudio: false,
                // 关闭摄像头
                publishVideo: false,
                // 关闭屏幕共享
                publishScreen: false,
            };

            // 清空远端用户列表
            // 离开后，房间里所有其他人都看不到了
            state.remoteUsers = [];

            // 标记为"已离开"
            // 就像闸机显示红灯，门关了
            state.isJoined = false;
        },


        // =========================================================
        // 规程三：远端用户加入（remoteUserJoin）
        // =========================================================
        //
        // 【Action 含义】有远端用户加入了房间
        //
        // 【生活比方】有人刷卡进入了会议室，系统自动给他在座位牌列表里加一个位置。
        //             典型场景是 AI 用户（机器人客服）加入房间。
        //
        // 【申请内容】payload = IUser 类型，包含新加入用户的信息
        //   {
        //     userId: "AiAgent",     // AI 的用户ID
        //     username: "懂小智",     // AI 的显示名称
        //     publishAudio: false,    // AI 的麦克风状态
        //     ...
        //   }
        //
        // 【档案更新内容】
        //   remoteUsers → 把新用户的卡片追加到列表末尾
        //                  就像会议室里新来一个人，在名单末尾加上一行
        //
        // 【触发时机】通常是 AI 用户加入房间时触发。
        //            当用户点击"开始 AI 对话"时，AI 会被加入 RTC 房间。
        remoteUserJoin: (state, { payload }) => {
            // 把新用户追加到远端用户列表
            // push 会把新卡片放到列表的最后面
            // 就像点名册上新来了一个人，名字写在最后
            state.remoteUsers.push(payload);
        },


        // =========================================================
        // 规程四：远端用户离开（remoteUserLeave）
        // =========================================================
        //
        // 【Action 含义】有远端用户离开了房间
        //
        // 【生活比方】有人离开会议室，系统自动把他的座位牌从名单里移除。
        //             典型场景是 AI 用户退出房间（对话结束）。
        //
        // 【申请内容】payload = { userId: "AiAgent" }
        //   只需要知道要离开的用户 ID 就行
        //
        // 【档案更新内容】
        //   remoteUsers → 从列表中移除该用户
        //                  找到对应 userId 的位置，然后删除
        //                  就像会议室名单上划掉一个人的名字
        //
        // 【实现细节】
        //   1. 用 findIndex 找到用户在数组中的索引位置
        //   2. 用 splice 把那一项删掉
        remoteUserLeave: (state, { payload }) => {
            // 找到要离开的用户在数组中的索引位置
            // findIndex 会返回第一个满足条件的元素的索引
            // 想象在名单上从上往下找，找到那个人的名字在哪一行
            const findIndex = state.remoteUsers.findIndex((user) => user.userId === payload.userId);

            // 从那个位置开始，删除 1 个元素（把自己删掉）
            // splice(位置, 数量) 会从指定位置删除指定数量的元素
            state.remoteUsers.splice(findIndex, 1);
        },


        // =========================================================
        // 规程五：切换场景（updateScene）
        // =========================================================
        //
        // 【Action 含义】切换当前选中的场景
        //
        // 【生活比方】会议室从"智能客服模式"切换到"数字人模式"。
        //             就像换了一个客服——不同的场景有不同的 AI 配置和能力。
        //             例如：从"纯语音客服"换成"数字人客服"。
        //
        // 【申请内容】payload = 场景 ID 字符串，如 "Custom" 或 "Agent"
        //
        // 【档案更新内容】
        //   scene → 填入新的场景 ID
        //           程序会根据新的 scene 去 sceneConfigMap 里查找对应的配置
        //           然后初始化相应的 RTC 配置
        //
        // 【注意】只更新 scene 字段，不更新 sceneConfigMap。
        //         sceneConfigMap 在初始化时从后端一次性获取，这里只是切换引用。
        //         就像只是改变了"当前在看哪份档案"，档案本身没有变。
        updateScene: (state, { payload }) => {
            // 更新当前场景 ID
            // 就像在档案上写"当前场景：Custom"
            state.scene = payload;
        },


        // =========================================================
        // 规程六：更新场景配置映射（updateSceneConfig）
        // =========================================================
        //
        // 【Action 含义】设置所有场景的配置（从后端获取后填充）
        //
        // 【生活比方】档案室收到一批新的"岗位说明书"（场景配置）。
        //             把这些说明书按编号分类放进抽屉里，方便以后查阅。
        //
        // 【申请内容】payload = {
        //   "Custom": {
        //     id: "Custom",
        //     name: "自定义助手",
        //     botName: "AiAgent_Custom",
        //     isVision: true,
        //     isAvatarScene: false,
        //     ...
        //   },
        //   "Agent": {
        //     id: "Agent",
        //     name: "智能体",
        //     botName: "AiAgent_Agent",
        //     isVision: true,
        //     isAvatarScene: true,
        //     ...
        //   }
        // }
        //
        // 【档案更新内容】
        //   sceneConfigMap → 替换为从后端获取的所有场景配置
        //                    这个映射表就像一本"场景配置字典"
        //                    程序通过 scene ID 来查找对应的完整配置
        updateSceneConfig: (state, { payload }) => {
            // 用后端返回的数据填充场景配置
            // 直接覆盖原来的空对象
            state.sceneConfigMap = payload;
        },


        // =========================================================
        // 规程七：更新 RTC 配置映射（updateRTCConfig）
        // =========================================================
        //
        // 【Action 含义】设置所有场景的 RTC 配置，同时初始化 RtcClient 的基础信息
        //
        // 【生活比方】档案室收到一批新的"入场凭证"（Token）。
        //             同时给 RTC 系统的"门禁设备"（RtcClient）设置好当前的门禁卡信息。
        //             门禁设备知道当前会议室的权限，才能正确开门。
        //
        // 【申请内容】payload = {
        //   "Custom": {
        //     AppId: "xxx",
        //     RoomId: "xxx",
        //     UserId: "xxx",
        //     Token: "xxx"
        //   },
        //   "Agent": { ... }
        // }
        //
        // 【档案更新内容】
        //   rtcConfigMap → 替换为从后端获取的所有 RTC 配置
        //
        // 【副作用】
        //   RtcClient.basicInfo → 同步设置当前场景的 RTC 基础信息
        //   把当前选中的场景（state.scene）的 AppId, RoomId, UserId, Token 写入全局实例
        //   这样 RTC SDK 才能正确初始化，加入正确的房间
        //
        //   就像给"门禁设备"设置好当前会议室的开门权限
        //   门禁设备记录了：会议室号是多少、谁能进、门禁卡密码是什么
        updateRTCConfig: (state, { payload }) => {
            // 用后端返回的数据填充 RTC 配置映射
            // 直接覆盖原来的空对象
            state.rtcConfigMap = payload;

            // 把当前场景的 RTC 配置同步到 RtcClient 全局实例
            // 后续 RTC SDK 会读取这个 basicInfo 来加入房间
            // 这里设置了门禁设备的"当前会议室"信息
            RtcClient.basicInfo = {
                // 当前场景的 AppId（火山引擎应用编号）
                app_id: payload[state.scene].AppId,
                // 当前场景的 RoomId（房间号）
                room_id: payload[state.scene].RoomId,
                // 当前用户的 ID
                user_id: payload[state.scene].UserId,
                // 入场 Token（门禁卡密码）
                token: payload[state.scene].Token,
            };
        },


        // =========================================================
        // 规程八：更新本地用户状态（updateLocalUser）
        // =========================================================
        //
        // 【Action 含义】局部更新"我"的状态
        //
        // 【生活比方】更新"我自己档案卡片"上的某些字段。
        //             比如"开一下麦克风"、"关一下摄像头"、"改一下名字"。
        //             只改你要改的，其他字段保持不变。
        //
        // 【申请内容】payload = 要更新的字段，例如：
        //   { publishAudio: true }  // 开麦克风
        //   { publishVideo: false } // 关摄像头
        //   { username: "张三" }     // 改名
        //   { publishAudio: true, publishVideo: true } // 同时开麦克风和摄像头
        //
        // 【档案更新内容】
        //   localUser → 保留原有的字段，用 payload 的字段覆盖/新增
        //                就像在一张卡片上划掉旧值，写上新值
        //                没有提到的字段保持原样不动
        updateLocalUser: (state, { payload }) => {
            // 更新"我"的档案
            // 合并操作：保留旧的字段，用新的字段覆盖
            state.localUser = {
                // 先复制所有现有的字段
                // 这样就不会丢失没有传入的字段
                ...state.localUser,
                // 再用传入的字段覆盖
                // 传入什么就更新什么，没传的不动
                ...(payload || {}),
            };
        },


        // =========================================================
        // 规程九：更新网络质量（updateNetworkQuality）
        // =========================================================
        //
        // 【Action 含义】更新网络质量指示
        //
        // 【生活比方】实时监测电话线路的信号强度。
        //             信号好 = 通话清晰；信号差 = 断断续续。
        //             就像手机右上角的信号格数。
        //
        // 【申请内容】payload = { networkQuality: 0~5 的数值 }
        //   0 = UNKNOWN（未知，还未检测到网络状态）
        //   1 = EXCELLENT（极好，信号满格，通话质量极佳）
        //   2 = GOOD（良好，信号不错，偶尔轻微卡顿）
        //   3 = POOR（较差，信号不好，经常卡顿）
        //   4 = BAD（差，信号很差，通话断断续续）
        //   5 = VBAD（极差，信号极差，几乎无法通话）
        //
        // 【档案更新内容】
        //   networkQuality → 填入新的网络质量值
        updateNetworkQuality: (state, { payload }) => {
            // 更新网络质量指示
            // 直接赋值，覆盖原来的值
            state.networkQuality = payload.networkQuality;
        },


        // =========================================================
        // 规程十：更新远端用户状态（updateRemoteUser）
        // =========================================================
        //
        // 【Action 含义】局部更新远端用户的状态（支持单个或批量更新）
        //
        // 【生活比方】远端有人（可能是 AI）改变了自己的状态。
        //             比如 AI 开麦克风说话了、AI 关摄像头了。
        //             需要更新他的档案卡片上的对应字段。
        //
        // 【申请内容】payload = IUser 或 IUser[]（支持单个或批量）
        //   单个：{ userId: "AiAgent", publishAudio: true }
        //   批量：[{ userId: "AiAgent", publishAudio: true }, { userId: "User02", ... }]
        //
        // 【档案更新内容】
        //   remoteUsers → 在列表中找到对应 userId 的用户卡片
        //                  用传入的字段更新那张卡片上的信息
        //
        // 【实现细节】
        //   1. 先把 payload 统一成数组（如果不是数组就包一层）
        //   2. 遍历每个用户，在 remoteUsers 里找到匹配的人
        //   3. 用展开运算符合并旧数据和新数据（部分覆盖）
        updateRemoteUser: (state, { payload }) => {
            // 统一成数组格式，方便后续遍历处理
            // 如果传入的不是数组，就包一层变成数组
            // 这样后面的 forEach 可以统一处理
            if (!Array.isArray(payload)) {
                payload = [payload];
            }

            // 遍历要更新的用户列表
            // 可能一次更新多个人
            payload.forEach((user: any) => {
                // 找到这个用户在整个 remoteUsers 数组中的索引位置
                // 在名单上找到这个人的名字在哪一行
                const findIndex = state.remoteUsers.findIndex((u) => u.userId === user.userId);

                // 在原数组中替换这个位置的数据
                // 先复制该用户原有的所有字段，再加上新的字段
                // 就像在卡片上划掉旧内容，写上新内容
                state.remoteUsers[findIndex] = {
                    // 先复制该用户原有的所有字段（保留没传入的字段）
                    ...state.remoteUsers[findIndex],
                    // 再用新传入的字段覆盖
                    ...user,
                };
            });
        },


        // =========================================================
        // 规程十一：更新通话时长（updateRoomTime）
        // =========================================================
        //
        // 【Action 含义】更新通话已经持续的时长
        //
        // 【生活比方】会议室里的计时器，每秒+1。
        //             加入房间后计时器开始走，离开后计时器归零。
        //
        // 【申请内容】payload = { time: 123 }，单位是秒
        //
        // 【档案更新内容】
        //   time → 填入新的通话时长
        //
        // 【触发时机】通常是定时器每秒调用一次，累加通话时长。
        updateRoomTime: (state, { payload }) => {
            // 更新通话时长（秒）
            // 直接赋值
            state.time = payload.time;
        },


        // =========================================================
        // 规程十二：标记自动播放失败（addAutoPlayFail）
        // =========================================================
        //
        // 【Action 含义】标记某个用户的音频自动播放失败
        //
        // 【生活比方】某些浏览器（Chrome、Safari）有"自动播放策略"。
        //             就像有些会议室规定"不能自动开音响，必须有人先手动按下播放键"。
        //             如果没有用户交互，浏览器不允许自动播放声音。
        //             此时系统会触发这个事件，标记谁遇到了这个问题。
        //
        // 【申请内容】payload = { userId: "AiAgent" }
        //
        // 【档案更新内容】
        //   autoPlayFailUser → 如果该用户不在列表里，就追加进去
        //                       如果已经在列表里，就不重复添加
        //
        // 【UI 效果】界面通常会弹出一个提示："请先点击页面以启用音频"
        addAutoPlayFail: (state, { payload }) => {
            // 取出当前的失败列表
            // 就像拿出"播放失败名单"这张纸
            const autoPlayFailUser = state.autoPlayFailUser;

            // 查找是否已有这个用户的记录
            // 如果列表里已经有了，就不要再加一次
            const index = autoPlayFailUser.findIndex((item) => item === payload.userId);

            // 如果列表中没有这个人（index === -1 表示没找到）
            if (index === -1) {
                // 追加到列表中
                // 就像在名单末尾写下这个人的名字
                state.autoPlayFailUser.push(payload.userId);
            }
        },


        // =========================================================
        // 规程十三：移除自动播放失败标记（removeAutoPlayFail）
        // =========================================================
        //
        // 【Action 含义】移除某个用户的自动播放失败标记
        //
        // 【生活比方】用户点击了页面，解除了浏览器的自动播放限制。
        //             此时把这个用户从"播放失败"名单里划掉。
        //
        // 【申请内容】payload = { userId: "AiAgent" }
        //
        // 【档案更新内容】
        //   autoPlayFailUser → 从列表中过滤掉这个用户
        //                       保留其他所有用户（过滤掉指定项）
        removeAutoPlayFail: (state, { payload }) => {
            // 取出当前的失败列表
            const autoPlayFailUser = state.autoPlayFailUser;

            // 过滤掉指定用户
            // filter 会返回一个新数组，只包含满足条件的元素
            // 保留所有不等于 userId 的项，等于 userId 的项被排除
            const _autoPlayFailUser = autoPlayFailUser.filter((item) => item !== payload.userId);

            // 替换为过滤后的新列表
            // 就像把旧的名单划掉，换成新的名单
            state.autoPlayFailUser = _autoPlayFailUser;
        },


        // =========================================================
        // 规程十四：清除所有自动播放失败标记（clearAutoPlayFail）
        // =========================================================
        //
        // 【Action 含义】一次性清除所有自动播放失败标记
        //
        // 【生活比方】所有用户都解决了音频播放问题。
        //             把整个"播放失败"名单清空。
        //
        // 【档案更新内容】
        //   autoPlayFailUser → 变成空数组 []（重置）
        //
        // 【触发时机】通常是离开房间时调用，重置所有状态。
        clearAutoPlayFail: (state) => {
            // 清空失败列表
            // 直接赋值空数组
            state.autoPlayFailUser = [];
        },


        // =========================================================
        // 规程十五：更新 AI 功能启用状态（updateAIGCState）
        // =========================================================
        //
        // 【Action 含义】更新 AI 对话功能是否启用
        //
        // 【生活比方】客服系统开机/关机。
        //             isAIGCEnable = true 相当于"客服系统已开机，准备接听"。
        //             isAIGCEnable = false 相当于"客服系统已关机"。
        //
        // 【申请内容】payload = { isAIGCEnable: true/false }
        //
        // 【档案更新内容】
        //   isAIGCEnable → 填入新的值
        //
        // 【注意】这个和 isAITalking 不同：
        //         isAIGCEnable = AI 功能开关（整体开关，客服系统的总电源）
        //         isAITalking = AI 是否正在说话（具体状态，客服当前在不在说话）
        //         可以理解为"总电源"和"工作状态"的区别
        //         总电源关了，客服肯定不工作；总电源开着，客服可能在工作也可能在休息
        updateAIGCState: (state, { payload }) => {
            // 更新 AI 功能启用状态
            state.isAIGCEnable = payload.isAIGCEnable;
        },


        // =========================================================
        // 规程十六：更新 AI 说话状态（updateAITalkState）
        // =========================================================
        //
        // 【Action 含义】更新 AI 是否正在说话
        //
        // 【生活比方】客服开始说话或停止说话。
        //             isAITalking = true → 客服正在电话里说"好的，我来帮您..."
        //             isAITalking = false → 客服停下来，等你说话
        //
        // 【申请内容】payload = { isAITalking: true/false }
        //
        // 【档案更新内容】
        //   1. isAIThinking → false（一旦开始说话，思考状态自动结束）
        //   2. isUserTalking → false（一旦 AI 开始说，用户说话状态也重置）
        //   3. isAITalking → 填入新的说话状态
        //
        // 【副作用】AI 开始说话时，自动重置"思考"和"用户说话"状态。
        //          逻辑：
        //          - 客服开始说话了，意味着他已经想好了 → 思考结束
        //          - 客服开始说话了，意味着轮到用户暂停 → 用户停止说话
        //          就像：电话里客服开始回答了，你也知道该闭嘴听他说了
        updateAITalkState: (state, { payload }) => {
            // AI 开始说话 = 思考结束
            // 脑子不用转了，嘴巴开始动
            state.isAIThinking = false;

            // AI 开始说话 = 用户应该暂停
            // 客服在说了，用户就别抢话了
            state.isUserTalking = false;

            // 更新 AI 说话状态
            state.isAITalking = payload.isAITalking;
        },


        // =========================================================
        // 规程十七：更新 AI 思考状态（updateAIThinkState）
        // =========================================================
        //
        // 【Action 含义】更新 AI 是否正在思考/处理
        //
        // 【生活比方】客服听到了你的问题，正在脑子里组织答案。
        //             这个时候嘴里不说话，但脑子在转。
        //
        // 【申请内容】payload = { isAIThinking: true/false }
        //
        // 【档案更新内容】
        //   1. isAIThinking → 填入新的思考状态
        //   2. isUserTalking → false（AI 开始思考 = 用户应该暂停）
        //
        // 【副作用】AI 开始思考时，自动重置用户说话状态。
        //          逻辑：客服开始认真想了，意味着轮到用户暂停说话。
        //          就像：客服键盘啦啦响，你也知道该等他想好了再听他说。
        updateAIThinkState: (state, { payload }) => {
            // 更新 AI 思考状态
            state.isAIThinking = payload.isAIThinking;

            // AI 开始思考 = 用户应该暂停
            // 客服在想问题了，用户就别继续说了
            state.isUserTalking = false;
        },


        // =========================================================
        // 规程十八：追加字幕消息（setHistoryMsg）——最复杂的规程
        // =========================================================
        //
        // 【Action 含义】向对话历史中添加一条字幕
        //
        // 【生活比方】这是最复杂的规程，理解起来就像整理电话客服的"通话记录本"。
        //
        // 【问题背景】
        //   AI 说话是"流式输出"的——就像客服不是一次性说完所有话，
        //   而是一个字一个字往外蹦，每蹦一个字就调用一次这个方法。
        //   所以需要判断：这个字是"新开一句话"还是"继续上一句"。
        //
        //   例如客服说"好的，我来帮你查一下订单"：
        //   - 每收到一个字的更新，就调用一次 setHistoryMsg
        //   - "好" → 新增一条字幕
        //   - "的" → 追加到上一条字幕
        //   - "，" → 追加到上一条字幕
        //   - "我" → 追加到上一条字幕
        //   - "来" → 追加到上一条字幕
        //   - "帮" → 追加到上一条字幕
        //   - ... 依此类推
        //
        // 【核心判断逻辑】
        //   1. 判断是 AI 说的还是用户说的（通过 user 字段判断）
        //      AI 的 user === botName（当前场景的 AI 用户名）
        //      AI 的 user 包含 "voiceChat_"（另一种 AI 的标识方式）
        //
        //   2. 判断上一句是否已经说完（是否完整）
        //      如果上一句已经完整 → 新开一条字幕
        //      如果上一句还没说完 → 追加到上一条字幕里
        //
        //   3. 数字人模式（isAvatarScene=true）的特殊逻辑
        //      数字人场景用 paragraph 字段来判断是否新开一句
        //      非数字人场景用 definite 字段来判断是否完整
        //      区别：数字人有明确的"分段"概念，非数字人是流式输出
        //
        // 【申请内容】payload = {
        //   text: "好的，我来",   // 要添加/追加的文字内容
        //   user: "AiAgent",     // 说话人 ID
        //   paragraph: false,   // 是否新开一个段落
        //   definite: false     // 是否完整（AI 是否还在说）
        // }
        //
        // 【档案更新内容】
        //   msgHistory → 根据上述判断逻辑，新增或追加字幕
        setHistoryMsg: (state, { payload }) => {
            // 从申请内容中提取段落标记和完整标记
            // paragraph: 是否新开一段话
            // definite: 这句话是否完整
            const { paragraph, definite } = payload;

            // 获取当前字幕列表的最后一条记录
            // at(-1) 是现代 JS 的写法，等价于 [length-1]
            // || {} 表示如果列表为空，就取一个空对象（避免访问空指针）
            // 为什么要取最后一条？因为要判断是追加还是新开
            const lastMsg = (state.msgHistory.at(-1) || {}) as Msg;


            // =============================================
            // 【判断第一步】判断这段话是 AI 说的还是用户说的
            // =============================================
            //
            // fromBot = true 表示是 AI（机器人客服）说的话
            // fromBot = false 表示是用户说的话
            //
            // 两种判断依据：
            //   1. user === 当前场景的 botName（如 "AiAgent_Custom"）
            //   2. user 包含 "voiceChat_" 前缀（另一种 AI 标识）
            //
            // 【生活比方】电话里听到的声音，要先判断是"客服"还是"客户"
            //             因为处理方式不一样：客服的话可能有延迟，用户的话实时显示
            const fromBot =
                // 判断方式一：user 等于当前场景的 botName
                payload.user === state.sceneConfigMap[state.scene].botName ||
                // 判断方式二：user 包含 "voiceChat_" 前缀（另一种 AI 标识）
                payload.user.includes('voiceChat_');


            // =============================================
            // 【判断第二步】判断"字幕追加模式"
            // =============================================
            //
            // currentSubtitleMode = 1 表示数字人模式（isAvatarScene=true）
            // currentSubtitleMode = 0 表示非数字人模式（普通语音对话）
            //
            // 【区别解释】
            //   数字人模式 = 电视主持人（有明确的"分段"概念，说完一段再下一段）
            //              paragraph 字段会明确标记"新开一段"或"继续"
            //              可以理解为：一段话说完了，才开始下一段
            //
            //   非数字人模式 = 电话客服（实时流式输出，分段概念不那么明确）
            //                 definite 字段标记"是否完整"
            //                 可以理解为：字在不断往外蹦，没画句号就继续
            //
            // 【生活比方】
            //   就像整理速记员的记录：
            //   - 数字人模式：速记员每说完一段话就分段，有明显停顿
            //   - 非数字人模式：速记员边听边写，没有明显分段
            const currentSubtitleMode = state.sceneConfigMap[state.scene].isAvatarScene ? 1 : 0;


            // =============================================
            // 【判断第三步】判断"上一条字幕是否已经完整"
            // =============================================
            //
            // lastMsgCompleted = true 表示上一句已经说完了（可以开新句了）
            // lastMsgCompleted = false 表示上一句还没说完（需要继续追加）
            //
            // 判断规则：
            //   1. 如果不是 AI 说的话（fromBot=false，即用户说的话）
            //      → 直接用 paragraph 字段判断
            //      → 用户说的话永远用 paragraph 来判断是否新开一句
            //
            //   2. 如果是 AI 说的话且是数字人模式（fromBot && currentSubtitleMode）
            //      → 用 paragraph 字段判断
            //      → 数字人模式有明确的段落概念，用 paragraph 就够了
            //
            //   3. 如果是 AI 说的话但非数字人模式（!currentSubtitleMode）
            //      → 用 definite 字段判断（是否完整）
            //      → 非数字人模式用 definite 来判断有没有结束（画句号没）
            //
            // 【生活比方】
            //   就像整理速记员写的记录：
            //   - 速记员每写完一句就画一个句号（lastMsgCompleted=true）
            //   - 速记员还没画句号说明还在继续说（lastMsgCompleted=false）
            //   - 用户说的话总是新开一行（用户说一句就是一句）
            //   - 数字人说的每段话之间有明显停顿（用 paragraph 判断）
            //   - 非数字人流式输出，用 definite 判断有没有结束（画句号没）
            const lastMsgCompleted =
                // 条件：不是AI说的 或者 是数字人模式
                // 结果：用 paragraph 判断（用户的话或数字人模式）
                //       否则用 definite 判断（非数字人的AI话）
                !fromBot || currentSubtitleMode ? lastMsg.paragraph : lastMsg.definite;


            // =============================================
            // 【执行分支】根据判断结果，决定是"新增一条字幕"还是"追加到上一条"
            // =============================================

            if (state.msgHistory.length) {
                // 【分支 A/B】如果字幕列表不为空（有历史记录）

                if (lastMsgCompleted) {
                    // 【分支 A】如果上一条已经说完了 → 新增一条字幕
                    //
                    // 上一句完整了，所以开一条新的字幕记录
                    //
                    // 【生活比方】速记员画了句号，现在开新的一行写下一句
                    state.msgHistory.push({
                        // 填入这句话的内容
                        value: payload.text,
                        // 记录说这句话的时间
                        time: new Date().toString(),
                        // 记录是谁说的
                        user: payload.user,
                        // 标记是否完整
                        definite,
                        // 标记是否新段落
                        paragraph,
                    });

                } else {
                    // 【分支 B】如果上一条还没说完 → 追加到上一条字幕
                    //
                    // 继续在上一条字幕里追加内容（AI 还在说上一句话）
                    //
                    // 【生活比方】上一句还没画句号，速记员继续往上一条记录里写字

                    if (fromBot && currentSubtitleMode) {
                        // 【数字人模式 + AI 说的话】直接字符串拼接（追加内容）
                        //
                        // 数字人模式下，内容是逐字追加的，直接拼在后面就行
                        // 就像在白板上继续写，字越写越长
                        //
                        // 例如：上一条是"好的，"，收到"我"后变成"好的，我来"
                        lastMsg.value += payload.text;

                    } else {
                        // 【非数字人模式 或 用户说的话】替换内容（覆盖式更新）
                        //
                        // 非数字人模式下，实时显示的是当前正在说的完整内容
                        // 因为是流式输出，每次收到的是到目前为止的全部内容
                        // 所以用替换而不是追加
                        //
                        // 例如：收到"好的"，显示"好的"；收到"好的我"，显示"好的我"
                        // （而不是"好好的我"）
                        lastMsg.value = payload.text;
                    }

                    // 更新这条字幕的其他元数据（时间和状态）
                    // 这些字段也要跟着更新

                    // 更新时间戳（说话又持续了一会儿）
                    lastMsg.time = new Date().toString();

                    // 更新段落标记
                    lastMsg.paragraph = paragraph;

                    // 更新完整标记
                    lastMsg.definite = definite;

                    // 更新说话人（防止中途换人）
                    lastMsg.user = payload.user;
                }

            } else {
                // 【分支 C】如果字幕列表为空（这是第一条字幕）
                //
                // 第一条字幕，直接新增，不用判断
                //
                // 【生活比方】速记员开始记录，这是本通电话的第一行字
                state.msgHistory.push({
                    // 填入第一句话的内容
                    value: payload.text,
                    // 记录开始时间
                    time: new Date().toString(),
                    // 记录说话人
                    user: payload.user,
                    // 标记段落
                    paragraph,
                });
            }
        },


        // =========================================================
        // 规程十九：清空对话历史（clearHistoryMsg）
        // =========================================================
        //
        // 【Action 含义】清空所有对话历史字幕
        //
        // 【生活比方】撕掉整本通话记录本，准备重新记录。
        //             就像换了一个新话题，旧的对话记录不要了。
        //
        // 【档案更新内容】
        //   msgHistory → 变成空数组 []（重置）
        clearHistoryMsg: (state) => {
            // 清空所有字幕记录
            // 直接赋值空数组
            state.msgHistory = [];
        },


        // =========================================================
        // 规程二十：标记打断状态（setInterruptMsg）
        // =========================================================
        //
        // 【Action 含义】标记 AI 被用户打断的状态
        //
        // 【生活比方】你打断了客服的说话。
        //             客服停下来听你说，然后继续。
        //             系统需要在被打断的那句话上做标记。
        //
        // 【逻辑步骤】
        //   1. 重置 AI 说话状态（isAITalking = false）
        //   2. 从最后一条字幕往前找（倒序遍历）
        //   3. 找到第一条还没说完的话（definite = false）
        //   4. 把这条字幕标记为"被打断"（isInterrupted = true）
        //
        // 【档案更新内容】
        //   1. isAITalking → false（AI 停止说话了）
        //   2. msgHistory → 在找到的那条字幕上设置 isInterrupted = true
        setInterruptMsg: (state) => {
            // AI 被打断了，停止说话状态
            // 客服不说了，等你说话
            state.isAITalking = false;

            // 如果没有字幕记录，直接返回，不用继续处理
            if (!state.msgHistory.length) return;

            // 从最后一条字幕往前遍历（倒序查找）
            // 为什么要倒序？因为用户打断的肯定是最近说的那句话
            // 就像从最新的通话记录往前找，找到第一条没说完的
            //
            // 例如：msgHistory = ["你好", "你的订单是12345", "正在处理中..."]
            // 从后往前找，找到"正在处理中..."这条还没说完
            for (let id = state.msgHistory.length - 1; id >= 0; id--) {
                // 取出当前遍历到的字幕
                const msg = state.msgHistory[id];

                // 如果这条字幕有内容（非空）
                if (msg.value) {
                    // 如果这句话还没说完（AI 被中断了，definite = false）
                    if (!msg.definite) {
                        // 标记为被打断
                        // 就像在通话记录上画个"被中断"的标记
                        state.msgHistory[id].isInterrupted = true;
                    }
                    // 找到后就停止遍历
                    // 只需要标记最近的一条，还没说完的那条
                    break;
                }
            }
        },


        // =========================================================
        // 规程二十一：清空当前对话状态（clearCurrentMsg）
        // =========================================================
        //
        // 【Action 含义】离开房间时一次性清空所有对话相关状态
        //
        // 【生活比方】会议结束了，关掉所有设备、清理所有记录。
        //             会议室恢复初始状态，等待下一场会议。
        //
        // 【档案更新内容】
        //   1. currentConversation → {}（清空实时对话）
        //   2. msgHistory → []（清空字幕历史）
        //   3. isAITalking → false（AI 停止说话）
        //   4. isUserTalking → false（用户停止说话）
        //
        // 【触发时机】通常是用户离开房间时调用
        clearCurrentMsg: (state) => {
            // 清空实时对话内容
            // 当前正在说的那些"正在输入中..."清掉
            state.currentConversation = {};

            // 清空所有字幕记录
            // 整本通话记录本撕掉
            state.msgHistory = [];

            // 重置 AI 说话状态
            state.isAITalking = false;

            // 重置用户说话状态
            state.isUserTalking = false;
        },


        // =========================================================
        // 规程二十二：切换字幕显示（updateShowSubtitle）
        // =========================================================
        //
        // 【Action 含义】切换字幕的显示/隐藏
        //
        // 【生活比方】电话通话时，打开/关闭实时字幕功能。
        //             开 = 实时显示对方说的话（方便听障人士或嘈杂环境）。
        //             关 = 只听不说，界面更简洁。
        //
        // 【申请内容】payload = { isShowSubtitle: true/false }
        //
        // 【档案更新内容】
        //   isShowSubtitle → 填入新的值
        updateShowSubtitle: (state, { payload }) => {
            // 更新字幕显示状态
            state.isShowSubtitle = payload.isShowSubtitle;
        },


        // =========================================================
        // 规程二十三：切换全屏模式（updateFullScreen）
        // =========================================================
        //
        // 【Action 含义】切换数字人/主界面的全屏显示模式
        //
        // 【生活比方】把视频通话的画面放大到整个屏幕。
        //             或者从全屏退回窗口模式。
        //
        // 【申请内容】payload = { isFullScreen: true/false }
        //
        // 【档案更新内容】
        //   isFullScreen → 填入新的值
        updateFullScreen: (state, { payload }) => {
            // 更新全屏状态
            state.isFullScreen = payload.isFullScreen;
        },


        // =========================================================
        // 规程二十四：设置自定义场景名称（updatecustomSceneName）
        // =========================================================
        //
        // 【Action 含义】设置用户自定义的场景名称
        //
        // 【生活比方】给会议室重新起个名字。
        //             比如把"会议室A"改成"张三专属会议室"。
        //             把 AI 的名字从"懂小智"改成"小王"。
        //
        // 【申请内容】payload = { customSceneName: "小王的专属助手" }
        //
        // 【档案更新内容】
        //   customSceneName → 填入用户自定义的名称
        updatecustomSceneName: (state, { payload }) => {
            // 更新自定义场景名称
            state.customSceneName = payload.customSceneName;
        },
    },
});


