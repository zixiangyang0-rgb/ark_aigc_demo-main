# -*- coding: utf-8 -*-
"""
RAG 服务模块：知识库检索服务（Retrieval-Augmented Generation）
================================================================

【开门见山 - 先说结论】
RAG = Retrieval-Augmented Generation，中文叫"检索增强生成"。

它的作用是：在 AI 回答问题之前，先去"知识库"里找一找有没有相关内容，
把找到的内容和用户的问题一起发给 LLM，让 AI 的回答更准确、更有依据。

为什么需要 RAG？
  因为 LLM（大语言模型）的知识是有截止日期的，而且可能会"瞎编"。
  比如你问"你们公司课程价格是多少？"，LLM 如果没有你的知识库，
  它要么说"我不知道"，要么瞎编一个价格——这是不能接受的。
  RAG 就是来解决这个问题的：让 AI 回答前先查一下"正确答案"。


【生活比喻 - 看病时的诊断过程】
想象你去医院看病：

  场景1（没有 RAG）：
    医生问："你哪里不舒服？"
    你说："我最近总是头疼，还有点发烧。"
    医生想了想，说："可能是感冒吧。"（凭空猜测，可能猜错）
    → 这就像没有 RAG 的 LLM：可能回答错误

  场景2（有 RAG）：
    医生问："你哪里不舒服？"
    你说："我最近总是头疼，还有点发烧。"
    医生先去翻了翻你的病历本和医学典籍（知识库检索）
    医生说："根据你的症状和病历，应该是病毒性感冒。我先给你开个血常规检查。"
    → 这就是 RAG：先查资料，再回答，更准确


【本项目中的实际应用】

  用户问："课程多少钱？"
    ↓
  rag_service.retrieve("课程多少钱？")
    ↓
  去知识库里找相关内容，假设找到：
    "课程A：4999元，包含...
     课程B：7999元，包含...
     报名联系：..."
    ↓
  把知识库内容 + 用户问题一起发给 LLM
    ↓
  LLM 回答："根据我们的课程信息，课程A是4999元，课程B是7999元..."
    ↓
  用户听到的是准确的价格，而不是 AI 瞎编的数字


【RAG 的核心概念 - 逐一拆解】

1. 知识库（Knowledge Base）
   就是一个存放文档的地方，可以是 PDF、Word、网页、数据库等。
   这些文档里包含了"企业私有知识"——LLM 训练时没见过的东西。

   例如：
     - 你的公司课程价格表
     - 员工手册里的请假制度
     - 产品说明书里的技术规格
     - FAQ 常见问题解答

2. 切块（Chunking）
   知识库里的文档通常很长（几十页的 PDF、上万字的文章）。
   不能把整篇文档都扔给 LLM，太贵了，也太乱了。

   所以要"切块"：把文档切成一小段一小段（chunk）。
   每段 200-500 个字符，包含一个完整的知识点。

   【生活比喻】
     一本 300 页的菜谱，不能一页页全给厨师看。
     切成单独的菜谱卡片：
       - 卡片1：红烧肉的做法（原材料、火候、步骤）
       - 卡片2：糖醋排骨的做法
       - 卡片3：宫保鸡丁的做法
     每张卡片独立完整，厨师需要哪道菜就找哪张卡片。

3. 向量化（Embedding）
   这是 RAG 最核心的技术。

   把每块文字（chunk）转换成一个"向量"——一串数字。
   这个过程叫"向量化"或"Embedding"。

   为什么叫"向量"？
     向量就是"有方向和大小"的东西。
     比如 [0.3, 0.8, 0.1, ...] 这样一个数组，就是一个向量。
     这个向量代表了这块文字的"语义特征"。

   为什么要转换？
     因为计算机处理数字比处理文字更高效。
     而且，通过比较两个向量的"距离"（数学运算），可以判断两段文字是否"语义相似"。

   【生活比喻 - 地图找朋友】
     想象一张二维地图（简化版）：
       - 你的朋友住在朝阳区，坐标 [3, 7]
       - 你的另一个朋友住在海淀区，坐标 [5, 3]
       - 你的第三个朋友住在通州区，坐标 [8, 9]

     现在你想找一个"住得和你最近的朋友"来借醋做菜。
     你算一下距离：
       - 朋友A（朝阳）：距离 2.3 公里 → 最近！
       - 朋友B（海淀）：距离 5.1 公里
       - 朋友C（通州）：距离 6.7 公里

     向量搜索的原理类似：
       - 知识库里每张"卡片"都有一个向量坐标
       - 用户的问题也被转换成一个向量坐标
       - 在向量空间里找"离得最近"的几张卡片
       - 距离最近 = 语义最相似

4. 向量数据库（Vector Database）
   专门用来存储和检索向量的数据库。
   它不是存普通数据的（普通数据库存文字、数字）
   而是存"向量"的，并且支持"按相似度搜索"。

   市面上常见的向量数据库：
     - Milvus（推荐，开源，支持本地部署）
     - Pinecone（云服务，不用自己运维）
     - Qdrant（Rust 写的，性能好）
     - Weaviate（支持混合检索）

5. 相似度搜索（Similarity Search）
   用户问一个问题 → 把问题也转换成向量
   → 在向量数据库里找"距离最近"的前 K 个 chunk
   → 把这些 chunk 的原文返回

   【生活比喻 - 百度/Google 搜索】
     你在搜索框输入"苹果手机"
     搜索引擎返回和"苹果手机"相关的网页
     虽然这些网页里不一定包含"苹果手机"这四个字
     但搜索引擎通过分析，认定这些页面和"苹果手机"的语义最相关

     RAG 的向量搜索原理类似：
       你问"手机贵不贵？"
       → 转换为向量
       → 在知识库中找到和"手机价格"语义最相关的几个 chunk
       → 返回原文

6. 上下文注入（Context Injection）
   找到相关 chunk 后，把这些 chunk 的原文作为"上下文"，发给 LLM。

   就像你考试时带了一张小抄：
     - 小抄上写着公式（知识库里的 chunk）
     - 你（LLM）看到题目（小抄上的公式）后，自己推导答案
     - 不是把小炒原封不动地抄上去，而是理解后用自己的话回答

7. 生成回答（Generation）
   LLM 拿到：用户的问题 + 知识库的上下文
   → 生成回答，这个回答是"基于知识库的"，更准确


【RAG 完整流程 - 分步图解】

  Step 1（离线）：知识库准备
    原始文档（PDF、Word...）
    → 切块（chunking）
    → 向量化（embedding）
    → 存入向量数据库（Milvus/Pinecone...）
    → 知识库就准备好了

  Step 2（在线）：用户提问
    用户问："课程多少钱？"
    → 把问题向量化（embedding）
    → 在向量数据库里搜索相似 chunk
    → 找到相关片段
    → 把片段 + 问题一起发给 LLM
    → LLM 生成回答
    → 返回给用户


【本模块的结构】

  class RAGService
      __init__()      → 初始化（目前只是打印提示，因为知识库功能未启用）
      retrieve()      → 核心方法：根据用户问题，从知识库检索相关内容


【注意事项】
  本项目中，RAG 功能目前是"未启用"状态（self.enabled = False）
  retrieve() 方法目前直接返回空字符串。
  原因是需要额外配置向量数据库（Milvus 等）。

  启用步骤（TODO）：
    1. 部署 Milvus 向量数据库（或使用 Pinecone 云服务）
    2. 把课程相关的文档切块、向量化，存入数据库
    3. 在 __init__ 中初始化向量数据库连接
    4. 在 retrieve() 中实现向量检索逻辑
"""

import sys
from typing import Optional


class RAGService:
    """
    RAG 服务类：封装知识库检索的核心逻辑
    ================================================================

    这个类的设计思路：
      - retrieve() 是核心方法：输入一个问题，输出知识库里找到的相关内容
      - 目前功能未启用，retrieve() 直接返回空字符串
      - 保留了完整的接口设计，后续接入向量数据库后，只需补充实现即可

    设计理念：
      宁可返回空，也不要出错。因为知识库检索失败不应该阻塞整个对话流程。
      用户还是能和 AI 对话，只是 AI 可能不知道具体的课程价格等信息。
    """

    def __init__(self):
        """
        初始化 RAG 服务
        ================================================================

        目前只是打印一个提示信息，因为知识库功能还没有启用。

        【为什么不直接启用？】
          因为启用 RAG 需要额外的依赖和配置：
            1. 一个运行中的向量数据库（Milvus / Pinecone / Qdrant...）
            2. embedding 模型（把文字转成向量的模型）
            3. 知识库文档（你需要先上传课程相关的文档）
            4. 切块和索引（把文档处理后存入向量数据库）

          这些都需要额外的部署和配置工作。

        【生活比喻 - 餐厅的前台系统】
          餐厅刚开业，前台还没有建立客户档案系统（知识库未配置）。
          但餐厅还是能营业——前台只能靠记忆回答客户的问题。
          当客户问"你们有什么菜？"时，前台只能说"我记不太清了，您看看菜单吧"。

          这就是 self.enabled = False 的状态：
            知识库功能没配置，但不影响对话功能正常工作。
            只是 AI 不知道具体的课程价格等信息。

        self.enabled = False
          → 知识库功能默认关闭
          → 调用 retrieve() 时会直接返回空字符串
          → 对话功能不受影响，只是缺少知识库增强
        """
        self.enabled = False
        print("ℹ️ [RAG] 知识库检索功能当前未启用（如需启用，请配置向量数据库）")

    async def retrieve(self, query: str) -> str:
        """
        根据用户问题，从知识库中检索相关内容
        ================================================================

        这是 RAG 服务的核心方法。

        【方法签名解析】
          async def retrieve(query: str) -> str
            → async def = 异步方法，用 await 调用
            → query: str = 输入用户的问题（字符串）
            → return: str = 输出知识库里找到的相关内容（字符串）

        【参数详解】
          @param query
            用户的问题，例如：
              - "课程多少钱？"
              - "报名有什么要求？"
              - "退款政策是什么？"

            这个 query 会经历以下处理：
              1. 向量化：用 embedding 模型把 query 转成向量
              2. 搜索：在向量数据库里找相似度最高的前 K 个 chunk
              3. 拼接：把 K 个 chunk 的原文拼接在一起
              4. 返回：返回拼接后的字符串


        【返回值详解】
          返回 str 类型，格式类似：
            "课程A：4999元，包含...
             课程B：7999元，包含...
             报名联系：xxx"

          可能返回空字符串的情况：
            1. self.enabled = False（知识库未启用）
            2. 知识库是空的（还没有导入文档）
            3. 搜索时没找到相似的内容（query 和知识库不匹配）
            4. 向量数据库连接失败


        【生活比喻 - 图书馆管理员】
          场景：你走进图书馆，问管理员："请问有没有关于 Python 编程的书？"

          管理员的思考过程：
            1. 你说的是"Python 编程"（query）
            2. 管理员在电脑上搜索（向量数据库检索）
            3. 电脑返回：《Python入门到精通》《Python网络编程》《Python数据分析》
            4. 管理员告诉你："我们有三本 Python 的书，这是书名和位置..."

          retrieve() 方法就是这个过程，只是管理员换成了代码。


        【完整 RAG 检索流程（待实现的代码）】
          当 self.enabled = True 时，retrieve() 方法应该做以下事情：

          Step 1: 向量化查询
            把用户的问题转成向量
            query_embedding = embed_model.encode(query)
            → 这需要一个 embedding 模型（如 text2vec-base-chinese）
            → embed_model 是 HuggingFace 或 OpenAI 的 embedding 工具

          Step 2: 向量相似度搜索
            在向量数据库中搜索最相似的 top_k 个 chunk
            results = vector_db.search(
                collection_name="course_knowledge",
                vector=query_embedding,
                top_k=3,  # 返回最相似的 3 个 chunk
            )
            → results 可能是这样的：
              [
                {"content": "课程A：4999元...", "score": 0.95},
                {"content": "课程B：7999元...", "score": 0.88},
                {"content": "报名联系：xxx", "score": 0.72}
              ]

          Step 3: 拼接上下文
            把搜索结果拼接成一段文本
            context = "\n\n".join([r["content"] for r in results])
            → "课程A：4999元...\n\n课程B：7999元..."

          Step 4: 返回
            return context
        """
        # --------------------------------------------------------
        # 开关检查：如果知识库功能未启用，直接返回空
        # --------------------------------------------------------
        # 为什么要这样设计？
        #   因为知识库检索可能失败（数据库挂了、网络断了等）
        #   但检索失败不应该导致整个对话流程崩溃
        #   所以用 self.enabled 做开关，没启用就直接返回空
        #
        # 【生活比喻】餐厅的 VIP 折扣功能
        #   餐厅有一个 VIP 折扣系统（知识库功能）。
        #   如果系统宕机了（enabled=False），不影响正常点餐。
        #   只是用户享受不了折扣（AI 享受不了知识库增强）。
        if not self.enabled:
            return ""

        try:
            # ================================================================
            # 这里是知识库检索的核心逻辑（目前是占位注释）
            # 完整的 RAG 实现通常包括以下几个步骤：
            # ================================================================

            # --------------------------------------------------------
            # Step 1: 向量化查询（Query Embedding）
            # --------------------------------------------------------
            # 把用户的问题转换成一个"向量"（一串数字）
            # 这个向量代表问题的"语义"
            #
            # 代码示例（需要安装 embedding 模型）：
            #   from sentence_transformers import SentenceTransformer
            #   embed_model = SentenceTransformer('paraphrase-MiniLM-L6-v2')
            #   query_embedding = embed_model.encode(query)
            #
            # 【生活比喻】把问题翻译成"数学语言"
            #   就像把中文翻译成英文：
            #     "课程多少钱" → [0.23, -0.45, 0.78, ...]
            #   翻译后的"语言"是数学语言，让计算机能计算"相似度"

            # --------------------------------------------------------
            # Step 2: 向量相似度搜索（Similarity Search）
            # --------------------------------------------------------
            # 在向量数据库中找到最相似的知识片段
            #
            # 代码示例（需要安装 Milvus 或其他向量数据库）：
            #   from pymilvus import connections, Collection
            #   connections.connect("default", host="localhost", port="19530")
            #   collection = Collection("course_knowledge")
            #   collection.load()
            #   results = collection.search(
            #       data=[query_embedding],
            #       anns_field="embedding",
            #       param={"metric_type": "IP", "params": {"nprobe": 10}},
            #       limit=3,
            #       output_fields=["content"]
            #   )
            #
            # 【生活比喻】在图书馆里找最相关的书
            #   管理员把你的问题转成关键词（向量）
            #   在图书馆索引系统里搜索
            #   找到 3 本最相关的书

            # --------------------------------------------------------
            # Step 3: 拼接上下文（Concatenate Context）
            # --------------------------------------------------------
            # 把多个检索结果拼接成一段文本
            #
            # 代码示例：
            #   context = "\n\n".join([hit.entity.get("content") for hit in results[0]])
            #
            # 【生活比喻】把多本书的相关章节复印下来
            #   管理员找到了 3 本相关的书
            #   把每本书的相关章节复印下来，订在一起
            #   这叠纸就是"context"（上下文）

            # --------------------------------------------------------
            # Step 4: 返回上下文（Return Context）
            # --------------------------------------------------------
            # 把拼接后的上下文作为返回值
            # 这个返回值会被传给 LLM，作为回答的背景知识
            #
            # return context
            #
            # 【生活比喻】管理员把复印的资料递给你
            #   "这是你要找的相关内容，请参考"

            # ================================================================
            # 推荐的技术栈（如果要实现 RAG）
            # ================================================================

            # 【向量数据库推荐】
            #   - Milvus（推荐！开源、支持本地部署、社区活跃）
            #   - Pinecone（云服务，不用自己运维，按量付费）
            #   - Qdrant（Rust 编写，性能好，有免费托管版）
            #   - Weaviate（支持混合检索：向量+关键词）
            #   - Chroma（轻量级，适合本地开发测试）

            # 【Embedding 模型推荐】
            #   中文：text2vec-base-chinese（哈工大开源）
            #   英文：text-embedding-ada-002（OpenAI）、paraphrase-MiniLM-L6-v2
            #   多语言：multilingual-e5-base（微软）

            # 【RAG 框架推荐】
            #   - LangChain（最流行，生态最全，但有点重）
            #   - LlamaIndex（专为 RAG 设计，比 LangChain 更轻量）
            #   - Dify（可视化平台，不用写代码就能搭 RAG）
            #   - Flowise（也是可视化，界面比 Dify 更简洁）

            # 【推荐组合（开箱即用）】
            #   Milvus + LangChain + text2vec-base-chinese
            #   或
            #   Pinecone + LlamaIndex + OpenAI embedding

            raise NotImplementedError("请先配置向量数据库以启用 RAG 功能")

        except NotImplementedError:
            # --------------------------------------------------------
            # 知识库未配置时返回空，不阻塞对话流程
            # --------------------------------------------------------
            # 【生活比喻】图书馆管理员说："不好意思，我们这没有相关书籍"
            #   这不是错误，只是一个状态
            #   对话可以继续，AI 用自己的知识回答（可能不太准确，但不阻塞）
            return ""

        except Exception as e:
            # --------------------------------------------------------
            # 其他异常处理
            # --------------------------------------------------------
            # 可能是向量数据库连接失败、网络超时、查询语法错误等
            # 打印错误日志，但返回空字符串，不阻塞对话
            #
            # 【生活比喻】图书馆系统崩溃了，管理员说："不好意思，系统出了点问题"
            #   餐厅还能正常营业，只是没有知识库增强功能
            print(f"⚠️ [RAG] 知识库检索出错: {e}", file=sys.stderr)
            return ""


