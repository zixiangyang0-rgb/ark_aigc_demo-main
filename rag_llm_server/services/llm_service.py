# -*- coding: utf-8 -*-
"""
LLM 服务模块：封装大模型对话调用（支持流式响应）
================================================================

【开门见山 - 先说结论】
这个模块负责调用"大语言模型"（LLM），也就是让 AI 真正开口说话的地方。

核心功能只有一个：
  chat_stream() 方法 → 流式调用大模型，边想边说，一点一点返回 AI 的回答

同时支持 RAG（知识库增强）：
  在调用 LLM 之前，先去知识库检索相关内容，
  把知识库内容和用户的问题一起发给 LLM，
  这样 AI 的回答会更准确，不会瞎编。


【生活比喻 - 4S店买车】
想象你走进一家 4S 店，想买一辆车：
  - 前台（rag_service）先查一下你的预约记录、之前的咨询内容（知识库检索）
    → "您好，王先生，上次您来看过 SUV，还问了价格"
  - 把这些背景信息交给销售顾问（LLM）
    → "王先生，我们新到了一批 SUV..."
  - 销售不是等想好了再说，而是边想边说（流式输出）
    → "这款车呢...它是最新款...有三个颜色..."
    → 你一边听，一边对感兴趣的地方提问
  - 这样销售对你的情况更了解，推荐更精准（知识库增强的效果）


【核心技术解析】

1. Ark SDK
   这是火山引擎提供的大模型调用工具，类似于 OpenAI 的 openai Python SDK。
   它的用法和 OpenAI SDK 几乎一样，如果你用过 OpenAI 的 SDK，上手会很快。
   - 初始化一个客户端：client = Ark(api_key="...")
   - 调用模型：client.chat.completions.create(model="...", messages=[...], stream=True)

2. 流式输出（Streaming）
   传统的 API 调用：你发一个问题，服务器想 5 秒，返回一整段回答。
   流式输出：你发一个问题，服务器边想边输出，你先听到前半句，后半句还在"想"。
   就像微信语音消息的实时转文字——对方说一句，你听到一句。

   【技术细节】
   - 使用 Python 的 yield 关键字实现
   - yield 和 return 的区别：
     return = 一口气把东西全给你，然后函数结束
     yield = 给一点，暂停一下，再给一点...函数还在运行
   - FastAPI 的 StreamingResponse 会自动迭代 yield 生成的数据，一点一点发给客户端

3. RAG（Retrieval-Augmented Generation）
   检索增强生成。
   核心理念：在调用 LLM 之前，先去知识库（向量数据库）里找找有没有相关内容。
   - 知识库 = 一堆文档，被切成小片段，转换成"向量"存入数据库
   - 向量 = 一串数字，代表文字的"语义"
   - 检索时，把你的问题也转成向量，在数据库里找"距离最近"的向量
   - 距离最近的向量 = 语义最相似的文档片段

   【举个例子】
   知识库里有一段："课程A 价格：4999元，课程B 价格：7999元"
   你问："学费多少？"
   → 系统发现"学费"和"价格"语义很接近 → 检索到这段内容
   → LLM 收到："知识库内容：课程A 价格：4999元... 用户问题：学费多少？"
   → LLM 回答："根据知识库，课程A 价格是 4999 元..."
   → 而不是瞎编一个数字


【模块结构】
  class LLMService
      __init__()        → 初始化 Ark 客户端
      chat_stream()     → 核心方法：流式调用 LLM
"""

import sys
from typing import List, Dict, Any, AsyncIterator

# volcenginesdkarkruntime = 火山引擎的大模型 SDK（类似于 openai 库）
from volcenginesdkarkruntime import Ark

from config import settings


class LLMService:
    """
    LLM 服务类：封装大模型对话的核心逻辑
    ================================================================

    这个类只做一件事：调用大语言模型，获取 AI 的回答。

    设计思路：
      - 单例模式：整个项目只创建一个实例（llm_service = LLMService()）
      - 为什么不每次都 new？因为创建 Ark 客户端有开销，复用更高效
      - 类似于餐厅只请一个大厨，所有订单都交给这一个大厨处理
    """

    def __init__(self):
        """
        初始化：创建 Ark 客户端
        ================================================================

        这里做的是"准备工作"：
          就像厨师长打开厨房的灶台系统，检查燃气是否正常、锅具是否齐全。

        【生活比喻 - 厨师长上班】
          早上 9 点，厨师长到餐厅：
            1. 打开厨房的电源和燃气总开关（初始化 SDK）
            2. 检查今天的食材库存（验证 API Key 是否有效）
            3. 预热烤箱、准备好砧板（初始化连接池）
          然后厨师长就准备好了，随时可以接单做菜。

        【代码解释】
          Ark(api_key=settings.ARK_API_KEY)
            → 创建一个大模型客户端，持有这个客户端就能调用火山引擎的 LLM
            → api_key 是你调用 LLM 的"通行证"，在火山引擎控制台获取
            → 如果 key 无效或过期，调用时会报错
            → base_url 不需要传，因为 settings 里通常已经配好了默认地址
        """
        self.client = Ark(api_key=settings.ARK_API_KEY)

    def chat_stream(
        self,
        messages: List[Dict[str, str]],
        rag_context: str = ""
    ) -> AsyncIterator:
        """
        流式调用大模型，返回 AI 的逐字回答
        ================================================================

        这是本模块最核心的方法，理解它就理解了整个 AI 对话的核心逻辑。


        【参数详解】

        @param messages
            类型：List[Dict[str, str]]
            含义：对话历史列表，记录了用户和 AI 之前的对话

            格式：
              [
                {"role": "system",   "content": "你是懂小智..."},     ← 系统提示词（AI 的人设）
                {"role": "user",     "content": "课程多少钱？"},     ← 用户的问题
                {"role": "assistant","content": "课程A 4999元..."},  ← AI 的回答
                {"role": "user",     "content": "能便宜吗？"},       ← 用户追问
                ...
              ]

            为什么要传对话历史？
              因为 LLM 本身没有"记忆"，每次调用都是独立的。
              如果不传历史，你问"能便宜吗？"，AI 不知道你在问什么课程。
              传入历史后，AI 能理解上下文："用户之前在问课程价格"

            【生活比喻 - 客服中心】
              你打电话给银行客服：
                "我要转账" → 客服说"请提供银行卡号" → 你说"6217..."
                → 客服说"好的，正在处理"
              客服能记住你说的话（上下文），不需要你每次都说"我要转账"
              messages 就是帮 AI "记住"对话历史的东西。


        @param rag_context
            类型：str（字符串，可以是空字符串）
            含义：知识库检索到的相关内容

            什么情况下有值？
              当 RAG 服务启用了，并且知识库里找到相关内容时。
              例如："课程A 价格:4999元\n课程B 价格:7999元"

            什么情况下是空字符串？
              RAG 服务没启用，或者知识库里没找到相关内容。

            有什么作用？
              把知识库内容作为背景知识发给 LLM。
              就像你去医院看病，医生先看了你的病历本（RAG 内容）再诊断。

            【生活比喻 - 考试作弊纸条】
              想象你在考试，旁边有一张知识库的"小抄"（rag_context）：
                - 没有小抄：只能靠自己的知识储备回答，可能记错
                - 有小抄：先看小抄上的内容，再结合自己的知识回答
                - 小抄越准确，分数越高（AI 回答越准确）


        @return
            类型：AsyncIterator（异步迭代器）
            含义：调用这个方法返回一个生成器，遍历时会依次得到每个词块（chunk）

            每个 chunk 是什么？
              类型是 ChatCompletionChunk，来自 volcenginesdkarkruntime 库。
              包含：
                - id：这次对话的 ID
                - choices：选择列表，通常只有一个 choice
                - choices[0].delta：增量内容，即 AI 新增的文字
                - choices[0].delta.content：具体的文字内容

            典型的 chunk 序列：
              chunk[0]: choices=[{"delta": {"content": "我"}}]        → AI 说了"我"
              chunk[1]: choices=[{"delta": {"content": "是"}}]       → AI 说了"是"
              chunk[2]: choices=[{"delta": {"content": "懂"}}]       → AI 说了"懂"
              chunk[3]: choices=[{"delta": {"content": "小"}}]       → AI 说了"小"
              chunk[4]: choices=[{"delta": {"content": "智"}}]       → AI 说了"智"
              ...

            流式输出的好处：
              1. 响应更快：不用等 AI 全部想完，用户立刻开始收到回复
              2. 体验更自然：就像和人对话，对方边想边说
              3. 节省等待焦虑：用户能看到 AI 在"打字"，知道系统在响应


        【完整调用示例】
          # 场景：用户问"课程多少钱"
          messages = [
              {"role": "user", "content": "课程多少钱"}
          ]
          rag_context = "课程A 价格:4999元\n课程B 价格:7999元"

          # 调用流式接口
          for chunk in llm_service.chat_stream(messages, rag_context):
              # 每次遍历得到一个 chunk
              delta_content = chunk.choices[0].delta.content
              print(delta_content, end="", flush=True)
              # flush=True 确保立即显示，不用等缓冲区满了才输出

          # 输出效果（边想边说）：
          # 根...
          # 根据...
          # 根据知识...
          # 根据知识库...
          # 根据知识库，课...
          # 根据知识库，课程A ...
          # ...


        【内部执行步骤】

          Step 1: 构造系统提示词（system prompt）
            system prompt = 告诉 AI "你应该扮演什么角色、遵循什么规则"

            如果有 rag_context（知识库内容）：
              → system_prompt = "你是懂小智课程顾问...请结合以下知识库内容回答...\n\n【知识库内容】\n{知识库内容}\n\n【回答要求】..."
            如果没有 rag_context：
              → system_prompt = "你是懂小智课程顾问...请友好地回答用户的课程相关问题..."

          Step 2: 组装完整的消息列表
            full_messages = [系统提示词] + 对话历史
            把系统提示词放在第一位，后面跟对话历史

          Step 3: 调用 LLM
            self.client.chat.completions.create(
                model=settings.ARK_ENDPOINT_ID,  # 模型 ID，如 "doubao-pro-32k"
                messages=full_messages,          # 完整的上下文
                stream=True,                     # 开启流式输出
                max_tokens=4096,                 # 最多生成 4096 个 token（约 3000 个汉字）
                temperature=0.7,                 # 随机性参数
            )

          Step 4: 逐块 yield 返回
            yield from stream
            把 LLM 返回的每个 chunk 直接转发出去
        """
        # ================================================================
        # 第一步：构造系统提示词
        # ================================================================
        # system_prompt = 系统级指令，决定了 AI 的"人设"和"行为准则"
        #
        # 【生活比喻 - 酒店前台培训】
        #   新员工入职时，人事会发一份"员工手册"：
        #   "你是懂小智课程顾问，负责回答用户关于课程的问题。
        #    如果用户问到价格，优先参考知识库内容。
        #    回答要简洁、有条理，因为用户是通过语音听的。"
        #
        #   系统提示词就是这份"员工手册"，告诉 AI：
        #   - 我是谁？（懂小智课程顾问）
        #   - 我该怎么做？（结合知识库、简洁有条理）
        #   - 什么不该做？（不要瞎编价格、不要说脏话等）

        if rag_context:
            # 有知识库内容 → 告诉 AI 优先使用知识库内容回答
            # 【生活比喻】就像员工手册里夹了一张"今日特价"纸条，
            #            前台在回答客户问题时，要优先参考这张纸条
            system_prompt = (
                "你是懂小智课程顾问，智能客服助手。"
                "请结合以下知识库内容回答用户问题。如果知识库中有相关信息，优先使用知识库内容。\n\n"
                "【知识库内容】\n"
                f"{rag_context}\n\n"
                "【回答要求】\n"
                "1. 如果知识库中有相关内容，直接基于知识库内容回答\n"
                "2. 如果知识库中没有相关内容，可以基于通用知识回答，但请说明这是通用建议\n"
                "3. 回答要简洁、有条理，适合语音播报（避免过长的专业术语）"
            )
        else:
            # 没有知识库内容 → 用通用的课程顾问人设
            # 【生活比喻】员工手册没有夹纸条，那就用通用的话术回答
            system_prompt = (
                "你是懂小智课程顾问，智能客服助手。"
                "请友好地回答用户的课程相关问题。"
                "回答要简洁、有条理，适合语音播报。"
            )

        # ================================================================
        # 第二步：组装完整的消息列表
        # ================================================================
        # full_messages = 系统提示词 + 对话历史 + 当前问题
        #
        # 【生活比喻 - 给大厨的工作单】
        #   前台递给大厨一张工作单：
        #     第一行：今天的工作要求（大厨要用这个牌子的酱油，要大火快炒）
        #     后面几行：之前顾客点的菜和做好的成品（对话历史）
        #     最后一行：顾客刚点的菜（当前问题）
        #   大厨看到完整的上下文，才能做出符合顾客口味的菜
        #
        # 【代码解释】
        #   [{"role": "system", "content": system_prompt}] → 系统提示词放第一位
        #   messages.extend(...) → 把对话历史追加到后面
        #   注意：messages 里不包含系统提示词，需要我们手动加
        full_messages = [{"role": "system", "content": system_prompt}]
        full_messages.extend(messages)

        # ================================================================
        # 第三步：调用 LLM 并流式返回
        # ================================================================
        #
        # 【生活比喻 - 大厨开始做菜】
        #   把工作单交给大厨，大厨开始做菜。
        #   餐厅里有传菜窗口，大厨做好一道菜，就从窗口递出去。
        #   客人不需要等所有菜做完才能开始吃，而是做好一道吃一道。
        #
        # 【代码解释 - chat.completions.create 参数】
        #
        #   model = "doubao-pro-32k"
        #     → 告诉 SDK 用哪个模型
        #     → 模型就像不同的厨师：
        #       - doubao-pro = 大厨，手艺好但速度慢
        #       - doubao-lite = 二厨，速度快但偶尔出错
        #       - Claude / GPT = 外聘的专家，更专业但更贵
        #
        #   messages = full_messages
        #     → 完整的上下文，包含系统提示词和对话历史
        #
        #   stream = True
        #     → 开启流式输出！这是最关键的参数
        #     → True = AI 的回答一点一点地返回（打字机效果）
        #     → False = 等 AI 完全想完，一次性返回完整回答
        #
        #   max_tokens = 4096
        #     → 限制 AI 回答的长度
        #     → 1 token ≈ 0.75 个英文单词 ≈ 1-2 个汉字
        #     → 4096 token ≈ 3000-6000 个汉字
        #     → 设置这个防止 AI 回答太长，浪费时间和 Token
        #
        #   temperature = 0.7
        #     → 随机性参数，范围 0-2
        #     → 0 = 完全确定，每次回答都一样（适合factual问答）
        #     → 0.7 = 有创意但不太离谱（适合客服对话）
        #     → 1.5+ = 很有创意，但可能开始胡说八道
        #
        #     【生活比喻】这个参数就像是调酒的度数：
        #       0度 = 白开水，永远一个味，但肯定不会醉（出错）
        #       7度 = 啤酒，有点味道，偶尔有点意外惊喜
        #       40度 = 白酒，很烈，很有个性，但喝多了可能说胡话
        #
        try:
            # 创建流式对话
            # 返回值 stream 是一个可迭代对象
            # 每次遍历它，得到一个 ChatCompletionChunk
            stream = self.client.chat.completions.create(
                model=settings.ARK_ENDPOINT_ID,
                messages=full_messages,
                stream=True,       # 开启流式输出
                max_tokens=4096,   # 最大输出 token 数
                temperature=0.7,   # 随机性参数
            )

            # ================================================================
            # 第四步：逐块返回
            # ================================================================
            # yield from stream
            #   → 把 stream 里的每个 chunk 原样转发出去
            #   → yield from = for x in stream: yield x
            #
            # 【生活比喻 - 传菜窗口】
            #   大厨（LLM）每做好一道小菜（一个 chunk），就从窗口递出去（yield）
            #   不用等整桌菜做完，客人就能开始吃
            #   服务员（yield from）负责把每一道菜从大厨那里端到客人桌上
            yield from stream

        except Exception as e:
            # ================================================================
            # 异常处理
            # ================================================================
            # 如果调用 LLM 过程中出错（比如网络断了、API Key 无效、模型不存在）
            # 打印错误日志，并静默返回（不抛出异常，避免整个对话流程崩溃）
            #
            # 【生活比喻 - 大厨出事了】
            #   场景：大厨突然生病了，或者厨房着火了
            #   处理：不能直接告诉客人"厨房完蛋了"，而是委婉地说"今天暂时无法服务"
            #   代码里就是：打印错误日志，函数正常返回（yield 不会执行，对话收到空响应）
            print(f"LLM 调用失败: {e}", file=sys.stderr)
            return


# ============================================================
# 全局单例导出
# ============================================================
#
# 【为什么要导出单例？】
#   因为创建 Ark 客户端有开销（网络连接、认证等）
#   如果每次调用 chat_stream() 都重新创建客户端，会很慢很浪费
#   所以在模块加载时就创建一个实例，整个项目复用这一个
#
# 【生活比喻 - 餐厅的大厨】
#   餐厅只请一个大厨（llm_service）
#   所有订单都交给这一个厨师处理
#   早上开业时大厨就上岗了（__init__ 执行）
#   整天都在这个厨房里做菜（chat_stream 被调用）
#   晚上打烊了大厨才下班（进程结束）
#
# 【使用方式】
#   from services.llm_service import llm_service
#   for chunk in llm_service.chat_stream(messages, rag_context):
#       print(chunk)
#
llm_service = LLMService()
